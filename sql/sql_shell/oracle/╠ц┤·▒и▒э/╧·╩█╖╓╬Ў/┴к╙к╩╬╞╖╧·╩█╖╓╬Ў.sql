declare

  vbegdate date;
  venddate date;

begin

  --vbegdate := to_date('2017-5-1', 'yyyy.mm.dd');
  --venddate := to_date('2017-5-31', 'yyyy.mm.dd');
  vbegdate := to_date('\(1,1)', 'yyyy.mm.dd');
  venddate := to_date('\(2,1)', 'yyyy.mm.dd');

  delete from tmp_test2;
  commit;
  insert into tmp_test2
    (date1,char1,char2,char3,char4,char5,char6,char7,char8,char9,num1,num2,num3)
    select r.fildate,
           store.code,
           store.name,
           h4v_goodssort.asname,
           h4v_goodssort.bsname,
           h4v_goodssort.csname,
           h4v_goodssort.dsname,
           goods.code,
           goods.code2,
           goods.name,
           goods.rtlprc,
           sum(decode(r.cls,
                      '零售',
                      1,
                      '批发',
                      1,
                      '成本差异',
                      0,
                      '成本调整',
                      0,
                      -1) * r.qty),
           sum(decode(r.cls,
                      '零售',
                      1,
                      '批发',
                      1,
                      '成本差异',
                      0,
                      '成本调整',
                      0,
                      -1) * (r.amt + r.tax))
      from sdrpt r
      left join h4v_goodssort
        on r.GDGID = h4v_goodssort.gid
      left join store
        on r.SND = store.gid
      left join goods
        on r.GDGID = goods.gid
     where r.gdgid in (select gid from h4v_goodssort where ascode = '26')
       and r.fildate >= vbegdate
       and r.fildate <= venddate
       and r.cls in
           ('零售', '零售退', '批发', '批发退', '成本差异', '成本调整')
     group by r.fildate,
              store.code,
              store.name,
              h4v_goodssort.asname,
              h4v_goodssort.bsname,
              h4v_goodssort.csname,
              h4v_goodssort.dsname,
              goods.code,
              goods.code2,
              goods.name,
              goods.rtlprc
     order by fildate;

  commit;

end;


