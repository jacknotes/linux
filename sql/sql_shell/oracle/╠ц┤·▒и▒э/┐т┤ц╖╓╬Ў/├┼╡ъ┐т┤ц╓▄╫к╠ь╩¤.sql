<REMARK>
[标题]
日期段门店库存周转天数
[应用背景]

[结果列描述]
销售额：查询本期内该门店销售金额（不包含周转材料）
销售成本：查询本期内门店的销售成本
平均销售成本：本期销售成本/月份天数
平均库存金额：本期内平均每天的库存金额，成本
库存周转率：if 销售额=0 or 库存金额=0 周转率=0
		else 周转率=销售成本/总库存金额=平均销售成本/平均库存金额
库存周转天数：平均库存金额/平均销售成本
[必要的查询条件]

日期

[实现方法]
mrpt,rpt_storesaledrpt,sinvdrpt
[其它]
</REMARK>
