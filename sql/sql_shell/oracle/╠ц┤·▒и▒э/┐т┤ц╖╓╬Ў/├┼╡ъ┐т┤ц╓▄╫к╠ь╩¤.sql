declare
  VSETTLENO VARCHAR2(10);
  VBDATE    DATE;
  VEDATE    DATE;
  
BEGIN  
  VSETTLENO := TRIM('\(1,1)');
  VBDATE :=  to_date(VSETTLENO,'yyyymm');
  VEDATE := LAST_DAY(to_date(VSETTLENO,'yyyymm'));
  
  DELETE FROM h4rtmp_zhouzhuan;
  DELETE FROM h4rtmp_zhouzhuan1;
  INSERT INTO h4rtmp_zhouzhuan1(settleno,STORE,SALETOTAL,SALEAMT,AVGSALEAMT)
  SELECT VSETTLENO,STORE.GID STORE, 
         SUM(decode( SMRPT.CLS,'零售退',-SMRPT.AMT,'成本调整',0,SMRPT.AMT)+decode( SMRPT.CLS,'零售退',-SMRPT.TAX,'成本调整',0,SMRPT.TAX)) TOTAL, 
         SUM(decode( SMRPT.CLS,'零售退',-SMRPT.IAMT,SMRPT.IAMT)+decode( SMRPT.CLS,'零售退',-SMRPT.ITAX,SMRPT.ITAX)) AMT,
         SUM(decode( SMRPT.CLS,'零售退',-SMRPT.IAMT,SMRPT.IAMT)+decode( SMRPT.CLS,'零售退',-SMRPT.ITAX,SMRPT.ITAX))/to_char(last_day(LAST_DAY(to_date(VSETTLENO,'yyyymm'))),'dd') AVGAMT
  FROM SMRPT SMRPT, GOODSH GOODSH, STORE STORE
  WHERE ((SMRPT.CLS in ('零售','零售退','成本调整','成本差异'))
  and  (SMRPT.GDGID = GOODSH.GID)
  and  (SMRPT.SND = STORE.GID) 
  and  (SMRPT.SETTLENO = VSETTLENO ) )
  GROUP BY STORE.GID;
 
  insert into h4rtmp_zhouzhuan1(settleno,store,inv,avginv) 
  select vsettleno,store,sum(iamt+itax) inv,sum(iamt+itax)/to_char(last_day(LAST_DAY(to_date(VSETTLENO,'yyyymm'))),'dd') avginv 
  from sinvdrpts , goodsh 
  where  GDGID = GID
  AND   FILDATE >= VBDATE
  AND   FILDATE <= VEDATE
  group by store;
  
  INSERT INTO h4rtmp_zhouzhuan (settleno,store,saletotal,saleamt,avgsaleamt,avginv,inv)
  SELECT H.SETTLENO,H.STORE,SUM(H.saletotal),SUM(H.saleamt),SUM(H.avgsaleamt),SUM(H.avginv),SUM(H.INV)
  FROM h4rtmp_zhouzhuan1 H,STORE ST
  WHERE   h.store = st.gid
  and     TO_CHAR(st.BUSDATE,'YYYYMM') <> vsettleno
  GROUP BY h.SETTLENO,h.STORE;
  
  COMMIT;
END;
