[应用背景]

[结果列描述]
    配货频率：是在查询时间段内有出货的商品的记录数在每天的平均值。如3天出了10次货，那么
每天出货频率是3.33
    应徐经理的要求，把缺货的记录数也当作出库数，以反应真实的市场需要。
[必要的查询条件]
         不包含整箱指的是不包含配货规格大于1的商品
