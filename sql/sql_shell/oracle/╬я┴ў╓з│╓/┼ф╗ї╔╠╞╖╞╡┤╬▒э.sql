DECLARE
  Vcount   Int;
  Vbgndate Date;
  Venddate Date;
  Vgdcnt   Int; ---商品总数
  Vcmd     Varchar2(1000);
  Vret     Int;
  Vcls     Varchar2(24);     
  Anayflag varchar2(24);   
  VMSG     VARCHAR2(255);  
  VUSERCODE varchar2(200);
  VLENGTH  INT;
  Vline		 int;
  Cursor c Is
    Select * From H4RTMP_ALCANALYZE Order By Fraquency Desc;
  Cursor c_Sort Is
    Select Distinct substr(Sort,1,VLENGTH) sort From H4RTMP_ALCANALYZE1;
  Cursor c_region Is
    select code from dc_slot_region;
Begin

    VUSERCODE := '\(USERCODE)';
delete TMP_VCODE;
insert into TMP_VCODE
select v.code  from psvdremp p ,employee e ,vendor v where p.vendor = v.gid and p.empgid = e.gid 
and e.code = VUSERCODE;

     DELETE FROM H4RTMP_ALCANALYZE;
     COMMIT;
     --VBGNDATE:=TO_DATE('2006.10.01','YYYY.MM.DD');
    -- VENDDATE:=TO_DATE('2006.11.01','YYYY.MM.DD');
     VBGNDATE:=TO_DATE('\(1,1)','YYYY.MM.DD');
     VENDDATE:=TO_DATE('\(2,1)','YYYY.MM.DD');
     VCLS:='\(4,1)';
     VLENGTH := 2 ; --按大类分析
     Anayflag := '\(5,1)';
 Select Trunc(Venddate) - Trunc(Vbgndate) Into Vcount From Dual;
  Insert Into H4RTMP_ALCANALYZE
    (Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls)
    Select a.Gdgid, b.Sort,region.code, Sum(a.Cnt), Sum(a.Fq), Vbgndate, Sum(a.Qty), Sum(nvl(a.Lackqty,0)),
           Vcls
      From (Select a.Gdgid, Count(a.Gdgid) Cnt, Round(Count(a.Gdgid) / Vcount, 4) Fq,
                    Sum(a.Qty) Qty, 0 Lackqty
               From Stkoutdtl a, Stkout b
              Where b.Num = a.Num
                And a.Cls = '统配出'
                And b.Cls = a.Cls
                And b.Stat In (700, 300)
                And b.Ocrdate < Venddate
                And b.Ocrdate >= Vbgndate
              Group By Gdgid
             Union All
             
             ---------------增加配货缺货数，这个是由于总部库存不足导致的，应该算作是商品出货频率的一部分
             Select Gdgid, Count(Gdgid) Cnt, Round(Count(Gdgid) / Vcount, 4) Fq, 0 Qty,
                    SUM(Reqqty - Acntqty) Lackqty
               From Gdlack
             Where Gendate < Venddate
                And Gendate >= Vbgndate
              Group By Gdgid) a, Goods b,da_slot_group DC ,dc_slot_region region
     Where a.Gdgid = b.Gid
       and Dc.Regioncode=Region.code
       and DC.Slotcode=b.slot
       and (Case When Not Exists (Select 1 From da_slot_group Where slotcode=b.slot)
                    Then '99999999' Else b.Slot End) =DC.Slotcode 
     Group By a.Gdgid,b.Sort, Region.code;

  --20070827增加有库存无配货商品
  Insert Into H4RTMP_ALCANALYZE
    (Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls)
    Select a.Gdgid, b.Sort,region.code, 0, 0, Vbgndate, 0, 0 , ''
      From Businvs a, Goods b,da_slot_group DC ,dc_slot_region region
     Where a.Store = 1000000
       And b.Gid = a.Gdgid
       And a.Wrh In (1, 1000002)
       And a.Qty <> 0
       and Dc.Regioncode=Region.code
       and (Case When Not Exists (Select 1 From da_slot_group Where slotcode=b.slot)
                    Then '99999999' Else b.Slot End) =DC.Slotcode 
       And a.Gdgid Not In (Select Gdgid From H4RTMP_ALCANALYZE);

  Select Count(Gdgid) Into Vgdcnt From H4RTMP_ALCANALYZE;

  If Vcls = '不包含整箱' Then
    Delete From H4RTMP_ALCANALYZE
     Where Exists (Select 1
              From Gdqpc
             Where Qpc <> 1
               And Isdu = 2
               And Gid = H4RTMP_ALCANALYZE.Gdgid);
  End If;

  If Anayflag = '类别' Then
    Vcmd := ''; ---通过一个临时表来对出货频次进行排序
    Begin
     Delete From H4RTMP_ALCANALYZE1 ; 
     Insert Into H4RTMP_ALCANALYZE1  (Gdgid, Sort, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls)
       Select Gdgid, Sort, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls From H4RTMP_ALCANALYZE  ;
    Exception
      When Others Then
        Null;
    End;
     
    Delete From H4RTMP_ALCANALYZE;
  
    For r_Sort In c_Sort Loop
    
      Insert Into H4RTMP_ALCANALYZE
        (Gdgid, Sort, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls , line)
        Select Gdgid, Sort, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls, Rownum
          From (Select Gdgid, Sort, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls
                   From H4RTMP_ALCANALYZE1
                  Where substr(Sort,1,VLENGTH) = r_Sort.Sort
                  Order By Fraquency Desc);
      Select Count(Gdgid) Into Vgdcnt From H4RTMP_ALCANALYZE Where substr(Sort,1,VLENGTH) = r_Sort.Sort;
    
      Update H4RTMP_ALCANALYZE
       Set Flag = 'A', Counts = Vcount
     Where Line <= Trunc(Vgdcnt * 20/ 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'B', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 20 / 100)
       And Line <= Trunc(Vgdcnt * 80 / 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'C', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 80 / 100)
       And Line <= Trunc(Vgdcnt * 95 / 100);
     Update H4RTMP_ALCANALYZE
       Set Flag = 'Z', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 95 / 100);

    end loop;
    commit;
    
             --------如果筛选条件为货区
    elsif Anayflag = '货区' Then
    Vcmd := ''; ---通过一个临时表来对出货频次进行排序
    Begin
     Delete From H4RTMP_ALCANALYZE1 ; 
     Insert Into H4RTMP_ALCANALYZE1  (Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls)
       Select Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls 
       From H4RTMP_ALCANALYZE ;
    Exception
      When Others Then
        Null;
    End;
     
    Delete From H4RTMP_ALCANALYZE;
  
    For r_region In c_region Loop
    
      Insert Into H4RTMP_ALCANALYZE
        (Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls , line)
        Select Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls, Rownum
          From (Select Gdgid, Sort,regioncode, Counts, Fraquency, Stardate, Alcqty, Lackqty, Cls
                   From H4RTMP_ALCANALYZE1
                  Where regioncode = r_region.code
                  Order By Fraquency Desc);
      Select Count(Gdgid) Into Vgdcnt From H4RTMP_ALCANALYZE Where regioncode = r_region.code;
    
     Update H4RTMP_ALCANALYZE
       Set Flag = 'A', Counts = Vcount
     Where Line <= Trunc(Vgdcnt * 20/ 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'B', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 20 / 100)
       And Line <= Trunc(Vgdcnt * 80 / 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'C', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 80 / 100)
       And Line <= Trunc(Vgdcnt * 95 / 100);
     Update H4RTMP_ALCANALYZE
       Set Flag = 'Z', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 95 / 100);
    
    End Loop;
 
  
    Commit;
  Else
    --------------使用序列号来排序，作为ABC分类的依据
    Vline := 0;
    For r In c Loop
      Vline := Vline + 1;
      Vret := 0;
      Update H4RTMP_ALCANALYZE
         Set Line = Vline
       Where Gdgid = r.Gdgid;
    
      Update H4RTMP_ALCANALYZE
         Set (Invqty,ordqty) = (Select Nvl(Sum(Qty), 0) , Nvl(Sum(OrdQty), 0)
                          From Businvs
                         Where Gdgid = r.Gdgid
                           And Store = 1000000
                           And Wrh In (1, 1000020))
       Where Gdgid = r.Gdgid;              
    
      Select Count(1)
        Into Vret
        From Gdqpc
       Where Isdu = 2
         And Gid = r.Gdgid;
      If Vret <> 0 Then
        Update H4RTMP_ALCANALYZE
           Set (Qpc, Qpcstr) = (Select Qpc, Qpcstr
                                   From Gdqpc
                                  Where Isdu = 2
                                    And Gid = r.Gdgid)
         Where Gdgid = r.Gdgid;
      Else
        Update H4RTMP_ALCANALYZE Set Qpc = 1.00, Qpcstr = '1*1' Where Gdgid = r.Gdgid;
      End If;
    End Loop;
  
     Update H4RTMP_ALCANALYZE
       Set Flag = 'A', Counts = Vcount
     Where Line <= Trunc(Vgdcnt * 20/ 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'B', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 20 / 100)
       And Line <= Trunc(Vgdcnt * 80 / 100);
    Update H4RTMP_ALCANALYZE
       Set Flag = 'C', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 80 / 100)
       And Line <= Trunc(Vgdcnt * 95 / 100);
     Update H4RTMP_ALCANALYZE
       Set Flag = 'Z', Counts = Vcount
     Where Line > Trunc(Vgdcnt * 95 / 100);
    Commit;
  End If;
   
   If Anayflag in ('货区' , '类别') then
          Update H4RTMP_ALCANALYZE
             Set (Invqty , ordqty) = (Select Nvl(Sum(Qty), 0),Nvl(Sum(OrdQty), 0)
                              From Businvs
                             Where Gdgid = H4RTMP_ALCANALYZE.gdgid
                               And Store = 1000000
                               And Wrh In (1, 1000020)) ;

            Update H4RTMP_ALCANALYZE
               Set (Qpc, Qpcstr) = (Select Qpc, Qpcstr
                                       From Gdqpc
                                      Where Isdu = 2
                                        And Gid = H4RTMP_ALCANALYZE.Gdgid);   
    end if; 

  ----      更新分析标志
  Update H4RTMP_ALCANALYZE Set Analyzeflag = Anayflag;

  Commit;

End;
