DECLARE
  afilldate date;
  bfilldate date;
  settleno  varchar2(10);
  ordNum    varchar2(20);
  Vendor1   varchar2(10);
  checkstatec  varchar2(10);
  checkstate integer;
begin
  ordNum    := trim('\(1,1)');
  afilldate := to_date('\(2,1)', 'yyyy.mm.dd');
  bfilldate := to_date('\(3,1)', 'yyyy.mm.dd');
  Vendor1   := trim('\(4,1)');
  settleno  := trim('\(5,1)');
  checkstatec := trim('\(6,1)');

  select case checkstatec  when '通过' then 1 when '未通过' then -1 when '未确认' then 0 end into checkstate  from dual;

  delete HD_REPORT.tmp_test2;
  commit;

  insert into HD_REPORT.tmp_test2
    (char1, ---订单号
     char2, --订单类型
     char3, ---期号
     char4, ---供应商代码
     char5, ---供应商名称
     date1, ---查看日期
     num1, --体积
     num2, --重量
     date2, --预计到货日期
     char10, --物流公司名称
     date3, --物流发货日期
     char6, ---物流单号
     char7, ---主要产品名称
     int1, ---件数
     char8, ------确认备注信息
     char9, ------确认状态
     date4 --确认日期
     
     )
  
    select ord.num, ---订单号
           ord.cls, --订单类型
           ord.settleno, ---期号
           vendor.code, ---供应商代码
           vendor.name, ---供应商名称
           ordstate.OPENDATE, --查看日期
           ordstate.VOLUME, --体积
           ordstate.WEIGHT, --重量
           ordstate.ARRIVEDAY, --预计到货日期
           ordstate.WULIUCOMPANY, ---物流公司名称
           ordstate.WULIUDAY, ---物流发货日期
           ordstate.WULIUNUM, ---物流单号
           ordstate.PRODUCTNAME, ---主要产品名称
           ordstate.GOODSCOUNT, ---件数
           ordstate.CHECKMENO, ---确认备注信息
           case ordstate.CHECKSTATE 
             when 0 then
              '未确认'
             when 1 then
              '通过'
             when -1 then
              '未通过'
           end CHECKSTATE, ---确认状态
           ordstate.CHECKDATE ---确认日期
      from ord, ordstate, vendor
     where ord.num = ordstate.num
       and ord.cls = ordstate.cls
       and ord.vendor = vendor.gid
       and ord.cls = '自营进'
       and ord.PURORDTYPE = 0
       and (ord.num = ordNum or ordNum is null)
       and ordstate.ARRIVEDAY between afilldate and bfilldate
       and (checkstate is null or ordstate.CHECKSTATE = checkstate )
       and (vendor.code = Vendor1 or Vendor1 is null)
       and (ord.settleno = settleno or settleno is null);

end;
