declare
  vbdate date;
  vedate date;
  vvdrcode varchar2(10);
  vgdcode varchar2(20);
begin
  delete from h4rtmp_ordexecute;
  commit;
  vbdate := to_date('\(1,1)','yyyy.mm.dd');
  vedate := to_date('\(2,1)','yyyy.mm.dd') + 1;
  vvdrcode := '\(3,1)';
  vgdcode := '\(4,1)';
  insert into h4rtmp_ordexecute(orddate,vdrcode,vdrname,ctgcode,ctgname,gdcode,gdname,commendqty,ordqty,diff1,acvqty,diff2,
                                diffrate1,diffrate2)
  select vbdate,v.code,v.name,s.code,s.name,g.code,g.name,
         sum(h.commendqty),sum(h.ordqty),sum(h.commendqty)-sum(h.ordqty),sum(h.acvqty),sum(h.ordqty)-sum(h.acvqty),
         0,0      
  from h4r_ordexecute h,goodsh g,vendorh v,sort s
  where h.vdrgid = v.gid and h.gdgid = g.gid and s.code = g.sort and v.code like vvdrcode||'%' and g.code like vgdcode||'%'
        and h.orddate >= vbdate and h.orddate <= vedate 
  group by vbdate,v.code,v.name,s.code,s.name,g.code,g.name;
  commit;
  update h4rtmp_ordexecute set 
    diffrate1 = (case when commendqty = 0 then 100 else round(ordqty/commendqty,4)*100 end),
    diffrate2 = (case when ordqty = 0 then 100 else round(acvqty/ordqty,4)*100 end);
  commit;
end;
