declare
pidate date ;
  cursor c is select ordnum,ordcls from h4r_ordexecute where stat in (200,300);
  vacvdate date;
  vsettleno int;
  VUSERCODE varchar2(200);
begin
  
VUSERCODE := '\(USERCODE)';
delete TMP_VCODE;
insert into TMP_VCODE
select v.code  from psvdremp p ,employee e ,vendor v where p.vendor = v.gid and p.empgid = e.gid and e.code = VUSERCODE;

  delete from h4r_ordexecute;
  pidate := sysdate;
  commit;
  insert into h4r_ordexecute(stat,orddate,vdrgid,ordnum,ordcls,gdgid,ordqty,commendqty,acvqty,lackqty,demanddate,deaddate,acvdate,ontime,loaddate)
  select mst.stat,og.time,mst.vendor,mst.num,mst.cls,dtl.gdgid,dtl.qty,dtl.recommendqty,dtl.acvqty,dtl.lackqty,mst.demanddate,mst.deaddate,'',mst.Finstat,pidate
    from ord mst,orddtl dtl,(select cls,num,trunc(time) time from ordlog where stat = 100) og
      where mst.num = dtl.num and mst.cls = dtl.cls and mst.cls = og.cls and mst.num = og.num and mst.fildate >= pidate-36500
        and mst.fildate < pidate and mst.stat in (100,200,300)
	and mst.PURORDTYPE<>1 ;   --20150702 只看订单，不查看采购计划单
  for r in c loop
    if r.ordcls = '自营进' then
      select min(time) into vacvdate from stkinlog
        where (num,cls) in (select num,cls from stkin where ordnum = r.ordnum and ordcls = r.ordcls)
          and stat = 1000;
    elsif r.ordcls = '直配出' then
      select min(time) into vacvdate from diralclog
        where (num,cls) in (select num,cls from diralc where ordnum = r.ordnum and ordcls = r.ordcls)
          and stat = 1000;
    end if;
    update h4r_ordexecute set acvdate = vacvdate where ordnum = r.ordnum and ordcls = r.ordcls;
    commit;
  end loop;
   
end;
