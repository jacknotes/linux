DECLARE
  afilldate date;
  bfilldate date;
  settleno  varchar2(10);
  ordNum    varchar2(20);
  Vendor1   varchar2(10);
  state     varchar2(20);
begin

  afilldate := to_date('\(1,1)', 'yyyy.mm.dd');
  bfilldate := to_date('\(2,1)', 'yyyy.mm.dd');
  settleno  := trim('\(3,1)');
  ordNum    := trim('\(4,1)');
  Vendor1   := trim('\(5,1)');
  state     := trim('\(6,1)');

  delete  HD_REPORT.tmp_test2;
  commit;

  insert into  HD_REPORT.tmp_test2
    (char1, ---订单号
     char2, --自营进
     char3, ---期号
     char4, ---供货单位
     char5, ---供货单位
     date1, ---需求日期
     date2, ---过期日期
     char6, ---填单人
     date3, ---填单日期
     char7, ---订单状态
     char8, ---仓位
     char9, ---来源单位
     date4, ---发送日期
     int1, ---发送次数
     char10, ---采购员
     int2, ---记录数  
     char11, ---入库单位
     char12, ---入库仓位  
     char13, ---是否发送至EC
     char14, ---查看状态
     char15, ---确认状态
     char16, ---确认备注
     char17, ---完成状态
     char18  ---供应商代码
     )
  
    select mst.num, ---订单号
           mst.cls, --自营进
           mst.settleno, ---期号
           TRIM(vdr.Name) || '[' || TRIM(vdr.Code) || ']' vendorname, ---供货单位
           TRIM(rcv.Name) || '[' || TRIM(rcv.Code) || ']' receivername, ---要货单位
           mst.DEMANDDATE, ---需求日期
           mst.DEADDATE, ---过期日期
           mst.FILLER, ---填单人
           mst.FILDATE, ---填单日期
           ms.StatName, ---订单状态
           TRIM(wrh.Name) || '[' || TRIM(wrh.Code) || ']' wrhname, ---仓位
           TRIM(src.Name) || '[' || TRIM(src.Code) || ']' srcname, ---来源单位
           mst.SNDTIME, ---发送日期
           mst.SENDCOUNT, ---发送次数
           mst.PSR, ---采购员
           mst.RECCNT, ---记录数  
           TRIM(rs.Name) || '[' || TRIM(rs.Code) || ']' raisername, ---入库单位
           TRIM(rsw.Name) || '[' || TRIM(rsw.Code) || ']' raiserwrhname, ---入库仓位  
           decode(mst.SndToEC, 0, '否', 1, '是') sendtoecname, ---是否发送至EC
           case
             when nvl(ORDSTATE.openState, 0) = 0 then
              '未查看'
             when ORDSTATE.openState = 2 then
              '已发货'
             when ORDSTATE.openState = 1 then
              '已查看'
           end openState, ---查看状态
           case nvl(ORDSTATE.checkState, 0)
             when 0 then
              '未确认'
             when 1 then
              '确认通过'
             when -1 then
              '确认未通过'
             else
              '无此状态'
           end checkState, ---确认状态
           ORDSTATE.Checkmeno, ---确认备注
           DECODE(MST.STAT || MST.FINSTAT,
                  '300N',
                  '未收货过期',
                  '300P',
                  '部分收货过期',
                  '300F',
                  '已完全收货',
                  '300',
                  '已完全收货',
                  '100',
                  '未收货',
                  '100N',
                  '未收货',
                  '100P',
                  '未收货',
                  '100F',
                  '未收货',
                  '200',
                  '部分收货',
                  '200N',
                  '部分收货',
                  '200P',
                  '部分收货',
                  '200F',
                  '部分收货',
                  '未收货') overfin, ---完成状态
           vdr.code   ---供应商代码
      from ORD        mst,
           VendorH    vdr,
           Store      rcv,
           ModuleStat ms,
           WarehouseH wrh,
           Store      src,
           Store      rs,
           WarehouseH rsw,
           --ORDLOG     log,
           ORDSTATE
     where mst.Vendor = vdr.GID
       and mst.Stat = ms.No
       --and mst.Num = log.Num
       --and mst.cls = log.cls
       and mst.Num = ORDSTATE.Num(+)
       and mst.cls = ORDSTATE.cls(+)
       --and log.stat = 100
       and mst.Wrh = wrh.GID(+)
       and mst.Src = src.GID
       and mst.Raiser = rs.Gid(+)
       and mst.RaiserWrh = rsw.Gid(+)
       and mst.Receiver = rcv.GID
       and mst.Cls = '自营进'
       and mst.PURORDTYPE = 1
       and mst.FILDATE >= afilldate
       and mst.FILDATE <= bfilldate
       and (mst.settleno = settleno or settleno is null)
       and (mst.num = ordNum or ordNum is null)
       and (vdr.code = Vendor1 or Vendor1 is null)
       and (state is null or
           (state = '未收货过期' and mst.stat = 300 and mst.finstat = 'N') or
           (state = '部分收货过期' and mst.stat = 300 and mst.finstat = 'P') or
           (state = '已完全收货' and mst.stat = 300 and
           (nvl(mst.finstat, 'F') = 'F')) or
           (state = '未收货' and mst.stat = 100) or
           (state = '部分收货' and mst.stat = 200))
     Order by FILDATE desc;
end;
