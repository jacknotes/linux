        0 QCqty,0 QCtax,0 QCamt,0 LSqty,0 LStax,0 LSamt,0 lsrealtax,0 lsrealamt,0 LSRTOTAL,
0 LSTqty,0 LSTtax,0 LSTamt,0 lstrealtax,0 lstrealamt,0 LSTRTOTAL,
0 CBCYQTY,0 CBCYTAX,0 CBCYAMT,0 PFQTY,0 PFTAX,0 PFAMT,0 pfrealtax,0 pfrealamt,
0 PFTQTY,0 PFTTAX,0 PFTAMT,0 pftrealtax,0 pftrealamt,
0 TPCqty,0 TPCtax,0 TPCamt,0 TPCTqty,0 TPCTtax,0 TPCTamt,
0 ZPCqty,0 ZPCtax,0 ZPCamt,0 ZPCTqty,0 ZPCTtax,0 ZPCTamt,
0 DBJqty,0 DBJtax,0 DBJamt,0 DBCqty,0 DBCtax,0 DBCamt,
0 SHqty,0 SHtax,0 SHamt,
0 YYqty,0 YYtax,0 YYamt,
0 PYqty,0 PYtax,0 PYamt,
0 PKqty,0 PKtax,0 PKamt,
0 QMqty,0 QMtax,0 QMamt,
0 Phcyqty,0 Phcytax,0 phcyamt,
0 Cbcy2qty,0 cbcy2tax,0 cbcy2amt,0 SCOREQTY,0 SCORETAX,0 SCOREAMT,0 Scorertotal,0 djcqty,0 djctax,0 djcamt,0 djcrtotal,
0 Cbzyqty,0 Cbzytax,0 Cbzyamt,0 Cbzycqty,0 Cbzyctax,0 Cbzycamt,0 Pdhcqty,0 Pdhctax,0 Pdhcamt,
0 KCJTZQTY ,0 KCJTZIAMT ,0 KCJTZITAX --库存价调整
 FROM GOODS G
  WHERE G.GID NOT IN (SELECT gdgid from q_JXCSDRPT where storegid = r.gid);

end loop;

  commit;
  
  if vstore is null then
    select zbgid into vzbgid from fasystem ;
    update q_JXCSDRPT set storegid = vzbgid;
  end if;
  commit;
end;
