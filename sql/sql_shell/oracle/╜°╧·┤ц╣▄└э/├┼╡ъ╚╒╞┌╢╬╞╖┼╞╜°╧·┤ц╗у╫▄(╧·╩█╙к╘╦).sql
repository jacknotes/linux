                      ZPCqty,ZPCtax,ZPCamt,ZPCTqty,ZPCTtax,ZPCTamt,
                      DBJqty,DBJtax,DBJamt,DBCqty,DBCtax,DBCamt,
                      SHqty,SHtax,SHamt,
                      YYqty,YYtax,YYamt,
                      PYqty,PYtax,PYamt,
