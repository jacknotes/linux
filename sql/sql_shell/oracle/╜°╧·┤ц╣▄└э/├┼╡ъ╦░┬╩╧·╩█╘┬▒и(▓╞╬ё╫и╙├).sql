                     a.QCqty,a.QCtax,a.QCamt,0 LSqty,0 LStax,0 LSamt,0 lsrealtax,0 lsrealamt,0 LSRTOTAL,
                     0 LSTqty,0 LSTtax,0 LSTamt,0 lstrealtax,0 lstrealamt,0 LSTRTOTAL,
                      a.CBCYQTY,a.CBCYTAX,a.CBCYAMT,(a.PFQTY+a.PFHHRqty) PFQTY,(a.PFTAX+a.PFHHRTAX),(a.PFAMT+a.PFHHRAMT),(a.PFTAX2+a.PFHHRTAX2) pfrealtax,(a.PFAMT2+a.PFHHRAMT2) pfrealamt,
