declare
vstorecode varchar2(24); 
cursor c(pistorecode varchar2) is
select g.name ,g.code,h.storecode,g.RTLPRC,g.code2,h.qty
from h4rtmp_goodshtm h,goodsh g
where h.newcode =g.code and h.storecode =pistorecode;
vnum number(24);
begin
delete  from h4rtmp_goodshtmdtl;
COMMIT;
vstorecode :='\(1,1)';
  vnum:=0;
  for r in c(vstorecode) loop
    vnum:=r.qty;
    while vnum>0 loop
      insert into h4rtmp_goodshtmdtl(name ,code,storecode,RTLPRC,code2)
      values(r.name,r.code,r.storecode,r.RTLPRC,r.code2);
    vnum:=vnum-1;
      end loop;
    end loop;  
COMMIT;
end;
