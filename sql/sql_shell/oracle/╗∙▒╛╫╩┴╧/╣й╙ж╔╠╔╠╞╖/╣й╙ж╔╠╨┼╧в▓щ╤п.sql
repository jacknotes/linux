  VendorCode varchar(20);             --供应商编号参数
  PLname     varchar(20);             --品类名称
  VendoName  varchar(50);             --供应商名称
  
  
begin
  VendorCode    :='\(1,1)';
  PLname        :='\(2,1)';                 --供应商参数  
