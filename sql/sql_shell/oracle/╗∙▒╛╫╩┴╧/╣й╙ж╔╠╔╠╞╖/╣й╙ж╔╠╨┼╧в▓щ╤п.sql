select 
       h4v_goodssort.asname,                                    --品类  
       VENDOR.CODE ,                                            --供应商编号，
       VENDOR.NAME ,                                            --供应商名称，
       goodsh.name,                                             --商品名字
       VENDOR.address ,                                         --工厂地址，
       VENDOR.contactor ,                                       -- 联系人，
       VENDOR.ctrphone ,                                        -- 联系方式，
       vendor.credit_code ,                                     --信用码，
       VENDOR.PAYMENT_DAYS ,                                    --账期，
       VENDOR.AMOUNT_GUARANTEE ,                                --质保金金额，
       VENDOR.AC_AGREEMENT ,                                    --是否签订年度协议，
       VENDOR.s_status ,                                         --合作状态（正常合作/淘汰/预淘汰），
       VENDOR.vat_invoice ,                                     --能否开票，
       VENDOR.ONLINE_SHARED_SALES_DATA ,                        --是否线上数据共享，
       min(stkin.ocrdate),                                      --首次下单时间，
       max(stkin.ocrdate),                                      --最后一次下单时间
       VENDOR.buyer                                             --采购员 
from stkin left join stkindtl on stkin.num=stkindtl.num
           left join VENDOR on stkin.vendor =VENDOR.gid 
           left join VDRAGMTDTLINV on VDRAGMTDTLINV.num =stkin.num  and VDRAGMTDTLINV.vdrgid =stkin.vendor
           and   VDRAGMTDTLINV.GDGID=stkindtl.gdgid
           left join goodsh on goodsh.gid=stkindtl.gdgid
           left join h4v_goodssort on h4v_goodssort.gid=stkindtl.gdgid and h4v_goodssort.gid=goodsh.gid
where  (VENDOR.CODE=VendorCode or VendorCode is null)
       and (h4v_goodssort.asname=PLname or PLname is null)
       and (VENDOR.NAME=VendoName or VendoName is null)          
group by                                  
       h4v_goodssort.asname,                                        --品类
       VENDOR.CODE ,                                                --供应商编号，
       VENDOR.NAME ,                                                --供应商名称，
       goodsh.name,                                                 --商品名字
       VENDOR.address ,                                             --工厂地址，
       VENDOR.contactor ,                                           -- 联系人，
       VENDOR.ctrphone ,                                            -- 联系方式，
       vendor.credit_code ,                                         --信用码，
       VENDOR.PAYMENT_DAYS ,                                        --账期，
       VENDOR.AMOUNT_GUARANTEE ,                                    --质保金金额，
       VENDOR.AC_AGREEMENT ,                                        --是否签订年度协议，
       VENDOR.s_status ,                                             --合作状态（正常合作/淘汰/预淘汰），
       VENDOR.vat_invoice ,                                         --能否开票，
       VENDOR.ONLINE_SHARED_SALES_DATA ,                            --是否线上数据共享，
       VENDOR.buyer                                                 --采购员 
               ;
commit;
end;
