declare
   vsettleno varchar2(20);--期号
   vbgndate date ;--开始日期
   venddate date ;--截止日期
    vstore varchar2(20);
begin
     vsettleno := '\(1,1)';--期号
   vbgndate := to_date('\(2,1)' ,'YYYY.mm.dd');--开始日期
   venddate := to_date('\(3,1)','YYYY.mm.dd') ;--截止日期
  vstore := '\(5,1)';
if vstore is null then
RAISE_APPLICATION_ERROR(-20000, '请选择查询门店范围');
end if;
if venddate-vbgndate > 30 then
RAISE_APPLICATION_ERROR(-20000, '查询日期请控制在30天以内');
end if;
 --  清空临时表
   delete from q_jxcsdrpt ;
  commit ;
  ---插入期初
  insert into q_jxcsdrpt(settleno,fildate ,Storegid ,Gdgid, QCqty,QCtax,QCamt)
   select JXCSDRPT.SETTLENO SNO, JXCSDRPT.fildate,JXCSDRPT.Storegid,JXCSDRPT.Gdgid, 
     QCqty,QCtax,QCamt
     FROM JXCSDRPT JXCSDRPT 
    WHERE JXCSDRPT.STOREGID = (SELECT GID FROM STORE WHERE CODE = vstore) and
    JXCSDRPT.SETTLENO = vsettleno 
     and JXCSDRPT.fildate = vbgndate
     ;
  --插入发生
INSERT INTO q_JXCSDRPT(SETTLENO,FILDATE,STOREGID,GDGID,QCqty,QCtax,QCamt,LSqty,LStax,LSamt,lsrealtax,lsrealamt,LSRTOTAL,
                     LSTqty,LSTtax,LSTamt,lstrealtax,lstrealamt,LSTRTOTAL,
                      CBCYQTY,CBCYTAX,CBCYAMT,PFQTY,PFTAX,PFAMT,pfrealtax,pfrealamt,
                      PFTQTY,PFTTAX,PFTAMT,pftrealtax,pftrealamt,
                      TPCqty,TPCtax,TPCamt,TPCTqty,TPCTtax,TPCTamt,
                      ZPCqty,ZPCtax,ZPCamt,ZPCTqty,ZPCTtax,ZPCTamt,
                      DBJqty,DBJtax,DBJamt,DBCqty,DBCtax,DBCamt,
                      SHqty,SHtax,SHamt,
                      YYqty,YYtax,YYamt,
                      PYqty,PYtax,PYamt,
                      PKqty,PKtax,PKamt,
                      QMqty,QMtax,QMamt,
                      Phcyqty,Phcytax,phcyamt,
                      Cbcy2qty,cbcy2tax,cbcy2amt,SCOREQTY,SCORETAX,SCOREAMT,Scorertotal,djcqty,djctax,djcamt,djcrtotal,
                      Cbzyqty,Cbzytax,Cbzyamt,Cbzycqty,Cbzyctax,Cbzycamt,Pdhcqty,Pdhctax,Pdhcamt,
                      KCJTZQTY ,KCJTZIAMT ,KCJTZITAX)
SELECT  SETTLENO,vbgndate,STOREGID,GDGID,
        SUM(nvl(0,0)),SUM(nvl(0,0)),SUM(nvl(0,0)),
        SUM(nvl(LSqty,0)),SUM(nvl(LStax,0)),SUM(nvl(LSamt,0)),SUM(nvl(lsrealtax,0)),SUM(nvl(lsrealamt,0)),SUM(nvl(LSrtotal,0)),
        SUM(nvl(LSTqty,0)),SUM(nvl(LSTtax,0)),SUM(nvl(LSTamt,0)),SUM(nvl(lstrealtax,0)),SUM(nvl(lstrealamt,0)),SUM(nvl(LStrtotal,0)),
        SUM(nvl(CBCYqty,0)),SUM(nvl(CBCYtax,0)),SUM(nvl(CBCYamt,0)),
        SUM(nvl(PFQTY,0)),SUM(nvl(PFTAX,0)),SUM(nvl(PFAMT,0)),SUM(nvl(pfrealtax,0)),SUM(nvl(pfrealamt,0)),
        SUM(nvl(PFTQTY,0)),SUM(nvl(PFTTAX,0)),SUM(nvl(PFTAMT,0)),SUM(nvl(pftrealtax,0)),SUM(nvl(pftrealamt,0)),
        SUM(nvl(TPCqty,0)),SUM(nvl(TPCtax,0)),SUM(nvl(TPCamt,0)),
        SUM(nvl(TPCTqty,0)),SUM(nvl(TPCTtax,0)),SUM(nvl(TPCTamt,0)),
        SUM(nvl(ZPCqty,0)),SUM(nvl(ZPCtax,0)),SUM(nvl(ZPCamt,0)),
        SUM(nvl(ZPCTqty,0)),SUM(nvl(ZPCTtax,0)),SUM(nvl(ZPCTamt,0)),
        SUM(nvl(DBJqty,0)),SUM(nvl(DBJtax,0)),SUM(nvl(DBJamt,0)),
        SUM(nvl(DBCqty,0)),SUM(nvl(DBCtax,0)),SUM(nvl(DBCamt,0)),
        SUM(nvl(SHqty,0)),SUM(nvl(SHtax,0)),SUM(nvl(SHamt,0)),
        SUM(nvl(YYqty,0)),SUM(nvl(YYtax,0)),SUM(nvl(YYamt,0)),
        SUM(nvl(PYqty,0)),SUM(nvl(PYtax,0)),SUM(nvl(PYamt,0)),
        SUM(nvl(PKqty,0)),SUM(nvl(PKtax,0)),SUM(nvl(PKamt,0)),
        SUM(nvl(0,0)),SUM(nvl(0,0)),SUM(nvl(0,0)),
        SUM(nvl(phcyqty,0)),SUM(nvl(phcytax,0)),SUM(nvl(phcyamt,0)),
        SUM(nvl(CBCY2qty,0)),SUM(nvl(CBCY2tax,0)),SUM(nvl(CBCY2amt,0)),
        SUM(nvl(SCOREqty,0)),SUM(nvl(SCOREtax,0)),SUM(nvl(SCOREamt,0)),SUM(nvl(SCORErtotal,0)),
        SUM(nvl(DJCqty,0)),SUM(nvl(DJCtax,0)),SUM(nvl(DJCamt,0)),SUM(nvl(DJCrtotal,0)),
        SUM(nvl(cbzyqty,0)),SUM(nvl(cbzytax,0)),SUM(nvl(cbzyamt,0)),
        SUM(nvl(cbzycqty,0)),SUM(nvl(cbzyctax,0)),SUM(nvl(cbzycamt,0)),
