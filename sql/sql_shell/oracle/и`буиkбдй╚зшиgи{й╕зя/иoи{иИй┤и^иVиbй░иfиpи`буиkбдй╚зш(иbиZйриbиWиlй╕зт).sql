DECLARE
  VBDATE   DATE;
  VEDATE   DATE;
  VWRH     VARCHAR2(10); 
  VSORT    VARCHAR2(10);
  VGDCODE  VARCHAR2(20);
  VBRAND   VARCHAR2(20);
BEGIN
	DELETE FROM H4RTMP_JXCDRPT;
	COMMIT;
	VBDATE := '\(1,1)';
	VEDATE := '\(2,1)';
	VWRH := '\(3,1)';
	VSORT := '\(4,1)';
	VGDCODE :='\(5,1)';
        VBRAND :='\(6,1)';
  INSERT INTO H4RTMP_JXCDRPT 
  (FILDATE,WRHCODE,WRHNAME,CTGCODE,CTGNAME,BRAND,BRANDNAME,GDCODE,GDNAME,
  GDGID,QCQTY,QMQTY,ZYQTY,TPQTY,PFQTY,SYQTY,YKQTY,DBQTY,CBZYQTY,PFCYQTY,
  QCTOTAL,QMTOTAL,ZYTOTAL,TPTOTAL,PFTOTAL,CBCYTOTAL,SYTOTAL,YKTOTAL,DBTOTAL,
  CBZYTOTAL,PFCYTOTAL,
  QCAMT,QMAMT,ZYAMT,TPAMT,PFAMT,CBCYAMT,SYAMT,YKAMT,DBAMT,CBZYAMT,PFCYAMT,
  QCTAX,QMTAX,ZYTAX,TPTAX,PFTAX,CBCYTAX,SYTAX,YKTAX,DBJTAX,CBZYTAX,PFCYTAX,
  LYQTY,LYAMT,LYTAX,LYIAMT,LYITAX,
  LYFHQTY,LYFHAMT,LYFHTAX,LYFHIAMT,LYFHITAX)
  SELECT
  VBDATE,W.CODE,W.NAME,S.CODE,S.NAME,B.CODE,B.NAME,G.CODE,G.NAME, G.GID,
  SUM(DECODE(J.FILDATE - VBDATE,0,1,0)*J.QCQTY),
  SUM(DECODE(J.FILDATE - VEDATE,0,1,0)*J.QMQTY),
  SUM(J.ZYJQTY-J.ZYJTQTY),
  SUM(J.TPCQTY-J.TPCTQTY),
  SUM(J.PFQTY-J.PFTQTY),
  SUM(J.YYQTY-J.SHQTY),
  SUM(J.PYQTY-J.PKQTY),
  SUM(J.DBJQTY-J.DBCQTY),
  SUM(J.CBZYRQTY-J.CBZYCQTY),
  SUM(J.PFRKQTY-J.PFCKQTY),
  SUM(DECODE(J.FILDATE - VBDATE,0,1,0)*(J.QCTAX+J.QCAMT)),
  SUM(DECODE(J.FILDATE - VEDATE,0,1,0)*(J.QMAMT+J.QMTAX)),
  SUM(J.ZYJAMT+J.ZYJTAX-(J.ZYJTAMT+J.ZYJTTAX)),
  SUM(J.TPCAMT+J.TPCTAX-(J.TPCTAMT+J.TPCTTAX)),
  SUM(J.PFAMT+J.PFTAX-(J.PFTAMT+J.PFTTAX)),
  SUM(J.CBCYAMT+J.CBCYTAX),
  SUM(J.YYAMT+J.YYTAX-(J.SHAMT+J.SHTAX)),
  SUM(J.PYAMT+J.PYTAX-(J.PKAMT+J.PKTAX)),
  SUM(J.DBJAMT+J.DBJTAX-(J.DBCAMT+J.DBCTAX)),
  SUM(J.CBZYRAMT+J.CBZYRTAX-(J.CBZYCAMT+J.CBZYCTAX)),
  SUM(J.PFRKAMT+J.PFRKTAX-(J.PFCKAMT+J.PFCKTAX)),
  SUM(DECODE(J.FILDATE - VBDATE,0,1,0)*(J.QCAMT)),
  SUM(DECODE(J.FILDATE - VEDATE,0,1,0)*(J.QMAMT)),
  SUM(J.ZYJAMT-(J.ZYJTAMT)),
  SUM(J.TPCAMT-(J.TPCTAMT)),
  SUM(J.PFAMT-(J.PFTAMT)),
  SUM(J.CBCYAMT),
  SUM(J.YYAMT-(J.SHAMT)),
  SUM(J.PYAMT-(J.PKAMT)),
  SUM(J.DBJAMT-(J.DBCAMT)),
  SUM(J.CBZYRAMT-(J.CBZYCAMT)),
  SUM(J.PFRKAMT-(J.PFCKAMT)),
  SUM(DECODE(J.FILDATE - VBDATE,0,1,0)*(J.QCTAX)),
  SUM(DECODE(J.FILDATE - VEDATE,0,1,0)*(J.QMTAX)),
  SUM(J.ZYJTAX-(J.ZYJTTAX)),
  SUM(J.TPCTAX-(J.TPCTTAX)),
  SUM(J.PFTAX-(J.PFTTAX)),
  SUM(J.CBCYTAX),
  SUM(J.YYTAX-(J.SHTAX)),
  SUM(J.PYTAX-(J.PKTAX)),
  SUM(J.DBJTAX-(J.DBCTAX)),
  SUM(J.CBZYRTAX-(J.CBZYCTAX)),
  SUM(J.PFRKTAX-(J.PFCKTAX)),
  SUM(J.LYQTY),SUM(J.LYAMT),SUM(J.LYTAX),SUM(J.LYIAMT),SUM(J.LYITAX),
  SUM(J.LYFHQTY),SUM(J.LYFHAMT),SUM(J.LYFHTAX),SUM(J.LYFHIAMT),SUM(J.LYFHITAX)
  FROM JXCDRPT J,WAREHOUSE W,GOODSH G,SORT S,BRAND B
    WHERE J.GDGID = G.GID AND J.WRH = W.GID AND G.SORT = S.CODE AND G.BRAND = B.CODE(+)
          AND J.FILDATE >= VBDATE AND J.FILDATE <= VEDATE AND W.CODE LIKE VWRH||'%' AND S.CODE LIKE VSORT||'%' AND G.CODE LIKE VGDCODE||'%' AND B.CODE LIKE VBRAND||'%'
      GROUP BY W.CODE,W.NAME,S.CODE,S.NAME,G.CODE,G.NAME,B.CODE,B.NAME,G.GID
      ORDER BY W.CODE,S.CODE,G.CODE;
  COMMIT;
END;
