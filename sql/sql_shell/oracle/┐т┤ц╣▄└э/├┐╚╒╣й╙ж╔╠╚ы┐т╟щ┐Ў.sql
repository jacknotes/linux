declare
     Bfiledate  date;
     Efiledate  date;
 begin
    Bfiledate  := to_date('\(1,1)', 'yyyy.mm.dd');
    Efiledate  := to_date('\(2,1)', 'yyyy.mm.dd');
    
 delete from tmp_test2;
 commit;
 
 insert into tmp_test2
 (
     char1,--进货方式   
     char2,--供应商
     char3,--仓库名称
     char4,--供应商名称
     int1, --商品款数
     int2, --数量
     num1, --含税金额
     date1 --发生日期
 
 )
 select STKIN.Cls,    --进货方式
        STKIN.Vendor, --供应商
        WAREHOUSE.Name,--仓库名称
        VENDOR.NAME,   --供应商名称
        count(distinct STKINDTL.gdgid) proqty, --商品款数
        sum(STKINDTL.qty), --数量
        sum(STKINDTL.Total),--含税金额
        max(Ocrdate)     --发生日期
   from STKIN, STKINDTL, VENDOR, WAREHOUSE
 
  where STKIN.Num = STKINDTL.Num
    and STKIN.Cls = STKINDTL.Cls
    and STKIN.Vendor = VENDOR.gid
    and STKIN.Wrh = WAREHOUSE.Gid
    and STKIN.Ocrdate > Bfiledate
    and STKIN.Ocrdate < Efiledate
  group by STKIN.Cls,
           STKIN.Vendor,
           VENDOR.NAME,
           WAREHOUSE.Name
  order by WAREHOUSE.Name;
  
   commit;
   end;
