DECLARE
  V_BGDATE      DATE;
  V_EDDATE      DATE;
  V_CKSEQ       INTEGER;
  V_STORECODE   VARCHAR2(10);

BEGIN
  DELETE FROM h4rtmp_CPOV_CKDATAS;
  COMMIT;

  V_BGDATE := TO_DATE(TRIM('\(1,1)'),'YYYY-MM-DD');
  V_EDDATE := TO_DATE(TRIM('\(2,1)'),'YYYY-MM-DD');
  V_CKSEQ := TRIM('\(3,1)');
  V_STORECODE := TRIM('\(4,1)');

IF V_CKSEQ IS NOT NULL AND V_STORECODE IS NOT NULL THEN
INSERT INTO h4rtmp_CPOV_CKDATAS(CKSEQ, CKDATE, CKSTAT, CKDATASTAT, STORECODE, STORENAME, SORTDCODE, SORTDNAME, GDGID, GDCODE, GDNAME, ACNTQTY, ACNTRTOTAL, QTY, RTOTAL, RTLBAL, COSTBAL)
