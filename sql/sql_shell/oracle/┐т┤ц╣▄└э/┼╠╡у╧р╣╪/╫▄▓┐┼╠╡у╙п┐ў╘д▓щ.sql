FROM WAREHOUSEH WRH, CKDataS CKDataS, GOODSH GOODSH, CKDir CKDir, V_SORTD V_SORTD
WHERE (CKDataS.WRH = WRH.GID)
 AND  (CKDataS.GDGID = GOODSH.GID)
 AND  (CKDir.CKSEQ = CKDataS.CKSEQ)
 AND  (GOODSH.SORT = V_SORTD.CODE)
 AND  (CKDir.CKSEQ = V_CKSEQ)
 AND  (WRH.CODE = V_STORECODE)
 AND  (CKDir.CKDATE >= NVL(V_BGDATE,TO_DATE('1900-1-1','YYYY-MM-DD')))
 AND  (CKDir.CKDATE <= NVL(V_EDDATE,TO_DATE('2900-1-1','YYYY-MM-DD')))
 AND  (CKDataS.STAT IN ('1','2','3'))
 AND CKDir.STAT ='0'
 AND CKDataS.STORE = 1000000
GROUP BY CKDir.CKSEQ, CKDir.CKDATE, CKDir.STAT, CKDataS.STAT, WRH.CODE, WRH.NAME, V_SORTD.CODE, V_SORTD.NAME, GOODSH.GID, GOODSH.CODE, GOODSH.NAME;
