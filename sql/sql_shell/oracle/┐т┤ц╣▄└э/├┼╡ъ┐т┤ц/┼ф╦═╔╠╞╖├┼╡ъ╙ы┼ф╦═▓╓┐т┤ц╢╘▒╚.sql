DECLARE
vsort varchar2(200);
vgdcode varchar2(200);
vstorecode varchar2(200);
VBRAND varchar2(200);
VWRHCODE varchar2(200);

BEGIN
  vstorecode :=trim('\(1,1)')||'%';
  vgdcode := trim('\(2,1)')||'%';
  vsort := trim('\(3,1)')||'%';
  VBRAND := trim('\(4,1)')||'%';
  VWRHCODE := trim('\(5,1)')||'%';
DELETE FROM H4RTMP_INVDIFF;
INSERT INTO H4RTMP_INVDIFF(STGID,STCODE,STNAME,GDGID,GDCODE,GDNAME,WCODE,WNAME,SOCODE,SONAME,BRCODE,BRNAME,STQTY,ZBQTY)
select alc.stgid,alc.stcode,alc.stname,alc.gdgid,alc.gdcode,alc.gdname,alc.wcode,alc.wname,alc.socode,alc.soname,alc.brcode,alc.brname,
nvl(inv.qty,0) stqty,nvl(zbinv.qty,0) zbqty 
 from (select s.gid stgid,s.code stcode,s.name stname,g.gid gdgid,g.code gdcode,g.name gdname,w.gid wgid,w.code wcode,w.name wname,
 b.wmsgid,so.code socode,so.name soname,br.code brcode,br.name brname
from alcscheme a ,alcschemedtl b ,store s,goodsh g,sort so,brand br,warehouse w
where a.code = b.code and a.code = s.alcscm and b.gdgid = g.gid and g.sort = so.code and g.brand = br.code(+)
and b.wmswrh = w.gid and b.alc = '统配'
AND (S.CODE LIKE vstorecode or vstorecode is null)
AND (G.CODE LIKE VGDCODE OR VGDCODE IS NULL)
AND (G.SORT LIKE VSORT OR VSORT IS NULL)
AND (G.BRAND LIKE VBRAND OR VBRAND IS NULL)
AND (W.CODE LIKE VWRHCODE OR VWRHCODE IS NULL)
) alc,
businvs inv ,businvs zbinv
where alc.stgid = inv.store(+) and alc.gdgid = inv.gdgid(+) and nvl(inv.wrh,1) = 1 
and alc.wmsgid = zbinv.store(+) and alc.wgid = zbinv.wrh(+) and alc.gdgid = zbinv.gdgid(+);



END;
