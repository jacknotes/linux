declare
  STORECODEA varchar2(20);
  STORENAMEA varchar2(20);
  GDCODEA    varchar2(20);
  GDCODE2A   varchar2(20);
  ascodeA    varchar2(20);
begin
  STORECODEA := '\(1,1)';
  STORENAMEA := '\(2,1)';
  GDCODEA    := '\(3,1)';
  GDCODE2A   := '\(4,1)';
  ascodeA    := '\(5,1)';

  delete from tmp_test2;
  commit;

  insert into tmp_test2
  
    (char1, ---门店代码
     char2, ---门店名称
     char3, ---仓位代码
     char4, ---仓位名称
     char5, ---类别代码
     char6, ---类别名称
     char7, ---商品代码
     char8, ---商品条码
     char9, ---商品名称
     int1, ---可用数量
     num1, ---售价
     int2, ---在单量(已审核定单)
     int3, ---未提数(已审核批发)
     int4, ---已配数(已审核配货)
     int5, ---待退数(已审核退供应商)
     int6, ---预留数
     int7, ---预留待配数(预留已审核配货)
     char10, ---中包装条码
     int8, ---中包装规格
     char11, ---大包装条码
     int9, ---大包装规格
     char12, ---供应商代码
     char13 ---供应商名称
     )
  
    SELECT STORE.CODE STORECODE, ---门店代码
           STORE.NAME STORENAME, ---门店名称
           DECODE(STORE.GID, 1000000, WAREHOUSE.CODE, '-') CODE1, ---仓位代码
           DECODE(STORE.GID, 1000000, WAREHOUSE.NAME, '未知') NAME1, ---仓位名称
           h4v_goodssort.ascode ascode, ---类别代码
           h4v_goodssort.asname asname, ---类别名称
           GOODSH.CODE GDCODE, ---商品代码
           GOODSH.CODE2 GDCODE2, ---商品条码
           GOODSH.NAME GDNAME, ---商品名称
           SUM(BUSINVS.QTY) QTY, ---可用数量
           GOODSH.RTLPRC RTLPRC, ---售价
           SUM(BUSINVS.ORDQTY) ORDQTY, ---在单量(已审核定单)
           SUM(BUSINVS.DSPQTY) DSPQTY, ---未提数(已审核批发)
           SUM(BUSINVS.ALCQTY) ALCQTY, ---已配数(已审核配货)
           SUM(BUSINVS.BCKQTY) BCKQTY, ---待退数(已审核退供应商)
           SUM(BUSINVS.RSVQTY) RSVQTY, ---预留数
           SUM(BUSINVS.RSVALCQTY) RSVALCQTY, ---预留待配数(预留已审核配货)
           max(AB.CODE), --- 中包装条码,
           max(AB.QPC), ----中包装规格
           max(AC.CODE), ---大包装条码,
           max(AC.QPC), ----大包装规格
           vendor.code, ----供应商代码
           vendor.name ----供应商名称
      FROM STORE STORE,
           WAREHOUSE WAREHOUSE,
           GOODSH GOODSH,
           BUSINVS BUSINVS,
           vendor,
           h4v_goodssort h4v_goodssort,
           (select max(gdinput.code) code, max(GDQPC.QPC) QPC, gdinput.gid
              from GDQPC, gdinput
             where GDQPC.Gid = gdinput.gid
               and GDQPC.QPCSTR = gdinput.qpcstr
               and GDQPC.isdu = 2
             group by gdinput.gid) AB,
           (select max(gdinput.code) code, max(GDQPC.QPC) QPC, gdinput.gid
              from GDQPC, gdinput
             where GDQPC.Gid = gdinput.gid
               and GDQPC.QPCSTR = gdinput.qpcstr
               and GDQPC.Ispu = 2
             group by gdinput.gid) AC
     WHERE ((BUSINVS.WRH = WAREHOUSE.GID) and (BUSINVS.STORE = STORE.GID) and
           (BUSINVS.GDGID = GOODSH.GID) and
           (GOODSH.GID = h4v_goodssort.gid))
       and GOODSH.Vdrgid = vendor.gid
       and (STORECODEA is null or STORE.CODE = STORECODEA)
       and (STORENAMEA is null or STORE.Name = STORENAMEA)
       and (GDCODEA is null or GOODSH.Code = GDCODEA)
       and (GDCODE2A is null or GOODSH.Code2 = GDCODE2A)
       and (ascodeA is null or h4v_goodssort.ascode = ascodeA)
          
       AND GOODSH.GID = ab.GID(+)
       AND GOODSH.GID = AC.GID(+)
     GROUP BY STORE.CODE,
              STORE.NAME,
              DECODE(STORE.GID, 1000000, WAREHOUSE.CODE, '-'),
              DECODE(STORE.GID, 1000000, WAREHOUSE.NAME, '未知'),
              h4v_goodssort.ascode,
              h4v_goodssort.asname,
              GOODSH.CODE,
              GOODSH.CODE2,
              GOODSH.NAME,
              vendor.code, ----供应商代码
              vendor.name, ----供应商名称
              GOODSH.RTLPRC
     ORDER BY STORECODE ASC, ascode ASC;

end;
