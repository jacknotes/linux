create or replace view v_businvs as
select 
GDGID,STORE,WRH,QTY,ORDQTY,DSPQTY,TOTAL,ALCQTY,BCKQTY,RSVQTY,RSV
ALCQTY
FROM BUSINVS
UNION ALL
select GID GDGID,1000000 STORE,WRH,0 QTY,0 ORDQTY,0 DSPQTY,0 
TOTAL,0 ALCQTY,0 BCKQTY,0 RSVQTY,0 RSVALCQTY
from goods
where (gid,wrh) not in (select gdgid,wrh from businvs where 
store = 1000000);
exec HDCREATESYNONYM('v_businvs');
