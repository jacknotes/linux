declare
VQTY  varchar2(20); 
VWRH  varchar2(20); 
begin

VQTY := '\(1,1)';
VWRH := '\(2,1)';

  delete from tmp_businvstat; commit;
  delete from tmp_businvstat1; commit;



/* 充足SKU数 */
insert into tmp_businvstat(sort1,sort2,sort3,sort4,gdcode,gdname,sku1,total1,wrh)
select substr(g.sort, 1, 2),
       substr(g.sort, 1, 4),
       substr(g.sort, 1, 6),
       substr(g.sort, 1, 8),
       g.code,
       g.name,
       count(b.gdgid),
       /*sum(b.qty*r.rtlprc),*/
       sum(b.qty),
       w.code
  from businv b, goods g,RPGGD r,warehouse w
 where b.gdgid = g.gid
   and g.code = r.inputcode
   and b.wrh =w.gid
   and b.STORE = 1000000
   and (w.code = VWRH or VWRH is null)
   and b.qty > VQTY 
 group by substr(g.sort, 1, 2),
          substr(g.sort, 1, 4),
          substr(g.sort, 1, 6),
           substr(g.sort, 1, 8),
          g.name,
          g.code,
          w.code;
 commit;
 
/* 缺货SKU数 */          
insert into tmp_businvstat(sort1,sort2,sort3,sort4,gdcode,gdname,sku2,total2,wrh)          
select substr(g.sort, 1, 2),
       substr(g.sort, 1, 4),
       substr(g.sort, 1, 6),
        substr(g.sort, 1, 8),
       g.code,
       g.name,
       count(b.gdgid),
        /*sum(b.qty*r.rtlprc),*/
        sum(b.qty),
          w.code
  from businv b, goods g,RPGGD r,warehouse w
 where b.gdgid = g.gid
   and g.code = r.inputcode
   and b.wrh =w.gid
   and b.STORE = 1000000
   and (w.code = VWRH or VWRH is null)
   and b.qty > 100
   and b.qty <= VQTY 
 group by substr(g.sort, 1, 2),
          substr(g.sort, 1, 4),
          substr(g.sort, 1, 6),
           substr(g.sort, 1, 8),
          g.name,
          g.code,
           w.code;
  commit;        
          
/* 断货SKU数 */          
insert into tmp_businvstat(sort1,sort2,sort3,sort4,gdcode,gdname,sku3,total3,wrh)          
select substr(g.sort, 1, 2),
       substr(g.sort, 1, 4),
       substr(g.sort, 1, 6),
        substr(g.sort, 1, 8),
       g.code,
       g.name,
       count(b.gdgid),
      /* sum(b.qty*r.rtlprc),*/
       sum(b.qty),
      w.code
  from businv b, goods g,RPGGD r,warehouse w
 where b.gdgid = g.gid
   and g.code = r.inputcode
   and b.wrh =w.gid
   and b.STORE = 1000000
   and (w.code = VWRH or VWRH is null)
   and b.qty <= 100
   and not exists (select 1 from orddtl u,ord o where o.num=u.num and o.cls = u.cls and  o.stat in (100,200) and u.GDGID = b.gdgid and u.lackqty>0)
 group by substr(g.sort, 1, 2),
          substr(g.sort, 1, 4),
          substr(g.sort, 1, 6),
           substr(g.sort, 1, 8),
          g.name,
          g.code,
          w.code;
commit;          
 
/* 在单SKU数 */         
