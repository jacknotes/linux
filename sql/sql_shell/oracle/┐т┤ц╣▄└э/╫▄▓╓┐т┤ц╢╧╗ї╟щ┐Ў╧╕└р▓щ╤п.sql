select 
       substr(g.sort, 1, 2),
       substr(g.sort, 1, 4),
       substr(g.sort, 1, 6),
       substr(g.sort, 1, 8),
       g.code,
       g.name,
       count(distinct b.GDGID),
      /* sum(b.ORDQTY * r.rtlprc),*/
       sum(b.ORDQTY),
       w.code
  from businv b, goods g,RPGGD r,warehouse w
 where b.GDGID = g.gid
   and g.code = r.inputcode
   and b.wrh =w.gid
   and b.STORE = 1000000
   and b.ORDQTY > 0    
 group by substr(g.sort, 1, 2),
          substr(g.sort, 1, 4),
