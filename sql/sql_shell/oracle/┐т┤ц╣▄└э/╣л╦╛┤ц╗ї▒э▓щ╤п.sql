declare
  settlenoA varchar2(20);

begin
  settlenoA := '\(1,1)';

  delete from tmp_test2;
  commit;

  insert into tmp_test2
    (int1, --结算期号
     char1, --类别
     int2, --数量
     Num1 ---金额
     )
    select settleno, ASNAME, sum(qty) Qty, sum(AMT) AMT
      from (select settleno, gdgid, sum(qmqty) Qty, sum(AMT) AMT
              from (select settleno, gdgid, qmqty, qmtax + qmamt AMT
                      from JXCSMRPT, h4v_goodssort sorA
                     where JXCSMRPT.settleno = settlenoA
                       and JXCSMRPT.GDGID = sorA.gid
                       and sorA.ascode not in ('23', '27', '21', '22', '20')
                       and JXCSMRPT.STOREGID not in
                           (select gid
                              from store
                             where code in ('40223',
                                            '40127',
                                            '40204',
                                            '40206',
                                            '40219',
                                            '40233',
                                            '40144',
                                            '40201',
                                            '40200'))
                    union all
                    select settleno, gdgid, qmqty, qmtax + qmamt AMT
                      from ZBJXC
                     where settleno = settlenoA) A
             group by settleno, gdgid) B
      left join h4v_goodssort sor
        on B.gdgid = sor.gid
    
     group by settleno, ASNAME;

end;
