      ,mst.ocrdate     --发生日期
      ,mst.settleno    --期号
      ,mst.reccnt      --记录数
      ,mstdtl.dmdqty   --退货数量
      ,mstdtl.qty      --实退数量
      ,g.code          --商品代码
      ,g.code2         --条形码
      ,g.name          --商品名称
      ,rcv.code        --收货单位代码
      ,rcv.name        --收货单位名称
      ,wrh.code        --仓位代码
      ,wrh.name        --仓位名称
