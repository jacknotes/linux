where mst.Client = clt.GID(+)   
  and mst.Receiver = rcv.GID(+)  
  and mst.Wrh = wrh.GID(+)  
  and mst.Src = src.GID  
  and mst.Stat = ms.No  
  and mst.Cls = '统配出退' 
  /*and mst.Stat = 400 */
  and mst.num=mstdtl.num
  and mst.cls=mstdtl.cls  
  and trunc(mst.fildate) between Bfiledate and Efiledate
  and mstdtl.gdgid=g.gid
  and clt.area=a.code
  and g.gid=h.gid
  and mst.bckcls=ran.no(+)
  and ran.Cls = 'STKOUTBCK'
  and mst.num=slog.num(+)
  and mst.cls=slog.cls(+)
  and slog.stat(+)=1000
order by clt.code;

commit;
end;
