declare
vdate date;
vstorecode varchar2(200);
begin
  vdate := to_date('\(1,1)','yyyy.mm.dd');
  vstorecode := trim('\(2,1)')||'%';

  delete from h4rtmp_nosale;
  commit;
  insert into h4rtmp_nosale(fildate,storegid,gdgid,invqty,
                            saleqty,saleqty7,saleqty14,saleqty30,saleqty60,saleqty90)
  select vdate,b.store,b.gdgid,b.qty+b.DSPQTY+b.ALCQTY+b.BCKQTY+b.RSVQTY+b.RSVALCQTY,
         sum(nvl(r.saleqty,0)),sum(nvl(r.saleqty7,0)),sum(nvl(r.saleqty14,0)),sum(nvl(r.saleqty30,0)),sum(nvl(r.saleqty60,0)),sum(nvl(r.saleqty90,0))
  from businvs b,(select * from RPT_storeSalDrpt where fildate = vdate) r
  where b.store = r.orgkey(+)
    and b.gdgid = r.pdkey(+)
    and b.qty > 0
    and b.store in ( select gid from store where code like vstorecode) 
  group by vdate,b.store,b.gdgid,b.qty+b.DSPQTY+b.ALCQTY+b.BCKQTY+b.RSVQTY+b.RSVALCQTY
  having  sum(nvl(r.saleqty30,0)) <= 0;
  
  commit;

end;
