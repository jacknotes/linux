declare 
  Bfiledate  date;
  Efiledate  date;

begin 
  Bfiledate  := to_date('\(1,1)', 'yyyy.mm.dd');
  Efiledate  := to_date('\(2,1)', 'yyyy.mm.dd');

  delete from tmp_test2;
  commit;

insert into tmp_test2
(
       char1          --店铺gid
      ,char2          --店号
      ,char3          --店名
      ,char4          --自定义类别
      ,char5          --类别
      ,char6          --商品名称
      ,num1           --账面数
      ,num2           --实盘数
      ,num3           --盘盈数
      ,num4           --盘盈金额
      ,num5           --盘亏数
      ,num6           --盘亏金额
      ,date1          --查询开始日期
      ,date2          --查询结束日期
)
select gid             --店铺gid
      ,code            --店号
      ,name            --店名
      ,sortname1       --自定义类别
      ,sortname        --类别
      ,goodsname       --商品名称
      ,sum(zms)        --账面数
      ,sum(sps)        --实盘数
      ,sum(pys)        --盘盈数
      ,sum(pyje)       --盘盈金额
      ,sum(pks)        --盘亏数
      ,sum(pkje)       --盘亏金额
      ,Bfiledate
      ,Efiledate
      
from(

select store.gid gid
      ,store.code code
      ,store.name name
      ,case when sort.code in ('40','42','43','44','45','46','47','48','49','50') then '日用百货类'
            when sort.code = ('41') then '食品类'
            when sort.code = ('20') then '商品辅料类'
            when sort.code = ('30') then '促销条码'
       end  sortname1 
      ,sort.name sortname
      ,goods.name goodsname
      ,CKDataS.Acntqty zms     --账面数  
      ,CKDataS.Qty sps             --实盘数
      ,(CKDataS.QTY - CKDataS.ACNTQTY) pys    --盘盈数 = 实盘数 - 账面数
      ,(CKDataS.Camtbal + CKDataS.Ctaxbal) pyje 
      ,0 pks
      ,0 pkje
       
from store , CKDataS , goods ,sort
where store.gid = CKDataS.Store
  and CKDataS.Gdgid = goods.gid
  and substr(goods.sort , 0 , 2) = sort.code
  and CKDataS.QTY - CKDataS.ACNTQTY > 0
  and CKDataS.Cktime >= '20150601' and CKDataS.Cktime <= '20150630'
  
  
  union all
  
  
select store.gid gid
      ,store.code code
      ,store.name name
      ,case when sort.code in ('40','42','43','44','45','46','47','48','49','50') then '日用百货类'
            when sort.code = ('41') then '食品类'
            when sort.code = ('20') then '商品辅料类'
            when sort.code = ('30') then '促销条码'
       end  sortname1 
      ,sort.name sortname
      ,goods.name goodsname
      ,CKDataS.Acntqty zms     --账面数  
      ,CKDataS.Qty sps             --实盘数
      ,0 pys
      ,0 pyje
      ,(CKDataS.QTY - CKDataS.ACNTQTY) pks    --盈亏数 = 实盘数 - 账面数
      ,(CKDataS.Camtbal + CKDataS.Ctaxbal) pkje    
       
from store , CKDataS , goods ,sort
where store.gid = CKDataS.Store
  and CKDataS.Gdgid = goods.gid
  and substr(goods.sort , 0 , 2) = sort.code
  and CKDataS.QTY - CKDataS.ACNTQTY < 0
  and CKDataS.Cktime >= '20150601' and CKDataS.Cktime <= '20150630'
  
) group by gid
           ,code
           ,name
           ,sortname1
           ,sortname
           ,goodsname       
   order by code ;        
   
   commit;
   end;
           
  
  
  
  
