      ,num14    --其他成本额
      ,date2    --开始日期
      ,date3    --结束日期
      ,char7    --品牌 于20170613日添加品牌字段
)
select store.gid                --店铺gid
      ,store.code               --店号
      ,store.name               --店名
      ,store.storeowner         --店铺所有者
      ,case  when store.storetype=1 then '合作店'
             when store.storetype=2 then '分公司'
             when store.storetype=3 then '直营店'  
             when store.storetype=4 then '加盟店'
        end as storetype        --门店性质
      ,store.storemanagertype   --门店管理权限
      ,store.FIN_AUTHORITY      --财务权限
      ,store.busdate            --开业日期
      ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax)) sal  --销售总额       
      ,sum(case when sort.code in ('10','11','12','13','15','16','17','18','19') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax)) end)  generalsal    --日用百货类
      ,sum(case when sort.code in ('14') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax))  end) foodsal    --食品类
      ,sum(case when sort.code in ('20') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax))  end) fuliaosal  --商品辅料类
      ,sum(case when sort.code in ('24') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax))  end)  gouwudaisal  --购物袋
      ,sum(case when sort.code in ('26') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax))  end)  lianyingsal  --联营
      ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax)) iamt --成本总额
      ,sum(case when sort.code in ('10','11','12','13','15','16','17','18','19') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax)) end)  generaliamt --日用百货成本额   
      ,sum(case when sort.code in ('14') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax))  end) foodiamt--食品成本额
      ,sum(case when sort.code in ('20') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax))  end) fuliaoiamt --商品辅料成本额
      ,sum(case when sort.code in ('24') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax))  end)  gouwudaiiamt  --购物袋成本额
      ,sum(case when sort.code in ('26') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax))  end)  lianying  --联营成本额
      ,sum(case when sort.code in ('21','22','23','27','25','30') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.amt+sdrpts.tax))  end)  qitaA  --其他成销售额
      ,sum(case when sort.code in ('21','22','23','27','25','30') then (decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',1,'成本调整',1,-1)*(sdrpts.iamt+sdrpts.itax))  end)  qitaB  --其他成本额
      ,Bfiledate
      ,Efiledate
      ,goods.brand --于20170613日添加品牌字段
from store ,sdrpts ,goods ,sort
where store.gid=sdrpts.snd
  and sdrpts.gdgid=goods.gid
  and substr(goods.sort,0,2)=sort.code
  and sdrpts.cls in ( '零售','零售退','批发','批发退','成本差异','成本调整')
  and sdrpts.fildate>= Bfiledate  and sdrpts.fildate<= Efiledate 
  and substr(goods.sort,0,2)!='20'
group by store.gid
        ,store.code
        ,store.name
        ,store.storeowner
        ,storetype
        ,store.storemanagertype
        ,store.busdate  
        ,store.FIN_AUTHORITY      --财务权限
        ,goods.brand
;

commit;
end;
