  Efiledate := to_date('\(2,1)');
  --Bfiledate := to_date('2017-6-1');
  --Efiledate := to_date('2017-6-12');
  
delete from tmp_test2;
  commit;  

insert into tmp_test2
(
       int1     --店铺gid
      ,char1    --店号
      ,char2    --店名
      ,char3    --店铺所有者
      ,char4    --门店性质
      ,char5    --门店管理权限
      ,char6    --财务权限
      ,date1    --开业日期
      ,num1     --销售总额
      ,num2     --日用百货销额
      ,num3     --食品销额
      ,num4     --商品辅料销额
      ,num5     --购物袋销额
      ,num11    --联营销额
      ,num6     --成本总额
      ,num7     --日用百货成本额
      ,num8     --食品成本额
      ,num9     --商品辅料成本额
      ,num10    --购物袋成本额
      ,num12    --联营成本额
