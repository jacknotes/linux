declare
     piVendorcode varchar2(20);
     piFinishdate date;
     vret int;
     vvendorgid int ;
     vbillto    int;
     poErr_Msg  varchar(255);
     vstore	int;

     cursor c is 
      select gid ,code , name from vendor where flag = '联销商' ;
begin
     piVendorCode :='\(1,1)';
     piFinishdate :=nvl(to_date('\(2,1)','yyyy.mm.dd'),trunc(sysdate));
     piFinishdate :=piFinishdate -1;
     
     select usergid into vstore from fasystem;

     delete from  H4RTMP_SALEBALBYGD ; commit;
     delete from  H4RTMP_SALEBALBYGDDTL ; commit;
   
     if  piVendorCode is not null then 
     --如果没有传入供应商，那么计算所有的联销供应商  
        select gid ,billto into vvendorgid , vbillto from vendor where code = piVendorCode ;
        vret := H4RTMP_DoGenSaleBalByGD_One(piVendor => vvendorgid  ,piFinishDate => piFinishdate  ,piOper => USER,poErr_Msg => poErr_Msg ) ;
        
        if vret <> 0 then
           insert into H4RTMP_SALEBALBYGD(VENDOR,BILLTO,store,finishdate,note )
           values(vvendorgid , vbillto , vstore ,piFinishdate, poErr_Msg );
        end if;

        commit;
     else 
       for r in c loop
   
        select gid ,billto into vvendorgid , vbillto from vendor where gid = r.gid ;
        vret := H4RTMP_DoGenSaleBalByGD_One(piVendor => vvendorgid  ,piFinishDate => piFinishdate  ,piOper => USER, poErr_Msg => poErr_Msg ) ;
        
        if vret <> 0 then
           insert into H4RTMP_SALEBALBYGD(VENDOR,BILLTO,store,finishdate,note )
           values(vvendorgid , vbillto ,vstore ,piFinishdate, poErr_Msg );
        end if;
         
        commit;

       end loop;
     end if;

end;
