declare
 vcode  varchar2(20);
 vbgndate date;
 venddate  date;
 begin
 delete from H4rTMP_alcstkout;
 commit;
  delete from H4rTMP_alcstkout3;
 commit;
  vcode:='\(1,1)';
  vbgndate:=to_date('\(3,1)','yyyy.mm.dd');
  venddate:=to_date('\(4,1)','yyyy.mm.dd');
   ----统配商品汇总
  insert into   H4rTMP_alcstkout
  (CODE,gdgid ,bgndate ,enddate ,alcprc  ,lstinprc ,  total ,rtotal,/*alcfee ,*/
lstintotal,
  invtotal,maoli,suiyi,cls)
  SELECT CLIENTH.GID,
         D.GDGID,
         vbgndate,
         venddate,
         round(d.price/d.qpc,4) alcprc,
         d.lstinprc,
         SUM(D.TOTAL),
         SUM(D.RTOTAL),
         sum(decode(d.lstinprc,0,g.cntinprc,d.lstinprc)*d.qty),
         sum(d.camt+d.ctax),
         sum(d.total - decode(d.lstinprc,0,g.cntinprc,d.lstinprc) * d.qty),
         sum(decode(d.lstinprc,0,g.cntinprc,d.lstinprc)* d.qty - (d.camt+d.ctax)),
         '批发'
    FROM STKOUT M, STKOUTDTL D, CLIENTH, STKOUTLOG MM, goodsh g
   WHERE  M.CLS = D.CLS
		and g.gid = d.gdgid
     AND M.NUM = D.NUM
     AND M.CLS = MM.CLS
     AND M.NUM = MM.NUM
     AND MM.CLS = '批发' 
     AND MM.STAT IN (700, 720, 740, 320, 340)
     AND M.BILLTO = CLIENTH.gid
     and MM.TIME >= vbgndate
     and MM.TIME < venddate
   GROUP BY CLIENTH.GID, D.GDGID,round(d.price/D.qpc,4),d.lstinprc,vbgndate,venddate;
   commit;
-----配货退货汇总
 insert into   H4rTMP_alcstkout
  (CODE,gdgid ,bgndate ,enddate ,alcprc  ,lstinprc ,  total ,rtotal,
lstintotal,
  invtotal,maoli,suiyi,cls)
          SELECT CLIENTH.GID,
                 D.GDGID,
                 vbgndate,
                 venddate,
                 round(d.price / d.qpc, 4) alcprc,
                 d.lstinprc,
                 -SUM(D.TOTAL),
                 -SUM(D.RTOTAL),
                 -sum(decode(d.lstinprc,0,g.cntinprc,d.lstinprc) * d.qty),
                 -sum(d.camt+d.ctax),
                 -sum(d.total - decode(d.lstinprc,0,g.cntinprc,d.lstinprc) * d.qty),
                 -sum(decode(d.lstinprc,0,g.cntinprc,d.lstinprc)* d.qty - (d.camt+d.ctax)),
                  '批发退'
             FROM STKOUTBCK M, STKOUTBCKDTL D,CLIENTH, STKOUTBCKLOG MM, goodsh g
            WHERE  M.CLS = D.CLS
		and g.gid = d.gdgid
              AND M.NUM = D.NUM
              AND M.CLS = MM.CLS
              AND M.NUM = MM.NUM
              AND MM.CLS = '批发退' 
              AND MM.STAT IN (1000, 1020, 1040, 320, 340)
              AND M.BILLTO = CLIENTH.gid       
              and MM.TIME >= vbgndate
              and MM.TIME < venddate 
             GROUP BY CLIENTH.gid, D.GDGID,round(d.price/D.qpc,4),d.lstinprc,vbgndate,venddate;
commit;
insert into H4rTMP_alcstkout3
  (code,
   bgndate,
   enddate,
   total,
   rtotal,
   lstintotal,
   invtotal,
   maoli,
   suiyi,
   cls)
  select code,
         bgndate,
         enddate,
         sum(total),
         sum(rtotal),
         sum(lstintotal),
         sum(invtotal),
         sum(maoli),
         SUM(suiyi),
         cls
    from H4rTMP_alcstkout
   group by code, bgndate, enddate, cls;
   commit;
end;
