DECLARE
  VBDATE  DATE;
  VEDATE  DATE;
  VNUM    VARCHAR2(2000);
  VGDCODE  VARCHAR2(20);
  VSTATNAME VARCHAR2(200);
  VFILTER1 VARCHAR2(1000);
  VCLS  VARCHAR2(14);
BEGIN

  VBDATE :=TO_DATE('\(1,1)','YYYY.MM.DD');
  VEDATE :=TO_DATE('\(2,1)','YYYY.MM.DD');
  VNUM :=TRIM('\(3,1)');
  --PPS_CONVERSTR(VFILTER1,',',VNUM);
  VGDCODE:=TRIM('\(4,1)');
 -- VSTATNAME :=TRIM('\(5,1)');
  VCLS := TRIM('\(5,1)');

  DELETE FROM H4RTMP_PHMX;
  COMMIT;
    ---插入批发出数据
  INSERT INTO H4RTMP_PHMX(NUM,CLS,GDCODE,GDNAME,GDCODE2,STNAME,QTY,QTYSTR,PRICE,TOTAL,TAX,CAMT,CTAX,RTOTAL,STATNAME,FILDATE,LINE,STCODE)
    SELECT STKOUT.NUM , STKOUT.CLS , GOODSH.CODE , GOODSH.NAME , GOODSH.CODE2 , 
      STORE.NAME , STKOUTDTL.QTY , STKOUTDTL.QTYSTR , 
      STKOUTDTL.PRICE , STKOUTDTL.TOTAL , STKOUTDTL.TAX , STKOUTDTL.CAMT , 
      STKOUTDTL.CTAX , STKOUTDTL.CRTOTAL , MODULESTAT.STATNAME,STKOUTLOG.TIME,STKOUTDTL.LINE,STORE.CODE
    FROM STKOUT STKOUT, STKOUTLOG STKOUTLOG, STKOUTDTL STKOUTDTL, GOODSH GOODSH, MODULESTAT MODULESTAT, CLIENT STORE
      WHERE ((STKOUT.NUM = STKOUTDTL.NUM)
       and  (STKOUT.CLS = STKOUTDTL.CLS)
       and  (STKOUT.NUM = STKOUTLOG.NUM)
       and  (STKOUT.CLS = STKOUTLOG.CLS)
       and  (STKOUT.STAT = MODULESTAT.NO)
       and  (STKOUTDTL.GDGID = GOODSH.GID)
       and  ((STKOUT.CLS = VCLS AND VCLS IS NOT NULL) OR (STKOUT.CLS ='批发' AND VCLS IS NULL))
       and  (STKOUT.BILLTO = STORE.GID)
       --and  (STKOUT.STAT = STKOUTLOG.STAT)
       and  (STKOUTLOG.STAT = 700)
       and  (STKOUTLOG.TIME >= VBDATE OR VBDATE IS NULL)
       and  (STKOUTLOG.TIME < VEDATE OR VEDATE IS NULL)
       and  (STKOUT.NUM LIKE VNUM||'%' OR VNUM IS NULL)
       and  (GOODSH.CODE LIKE VGDCODE||'%' OR VGDCODE IS NULL) );
  COMMIT;
    ---插入批发出退数据
  INSERT INTO H4RTMP_PHMX(NUM,CLS,GDCODE,GDNAME,GDCODE2,STNAME,QTY,QTYSTR,PRICE,TOTAL,TAX,CAMT,CTAX,RTOTAL,STATNAME,FILDATE,LINE,STCODE)
    SELECT STKOUT.NUM , STKOUT.CLS , GOODSH.CODE , GOODSH.NAME , GOODSH.CODE2 , 
      STORE.NAME , STKOUTDTL.QTY , STKOUTDTL.QTYSTR , 
      STKOUTDTL.PRICE , STKOUTDTL.TOTAL , STKOUTDTL.TAX , STKOUTDTL.CAMT , 
      STKOUTDTL.CTAX , STKOUTDTL.CRTOTAL , MODULESTAT.STATNAME,STKOUTLOG.TIME,STKOUTDTL.LINE,STORE.CODE
    FROM STKOUTBCK STKOUT, STKOUTBCKLOG STKOUTLOG, STKOUTBCKDTL STKOUTDTL, GOODSH GOODSH, MODULESTAT MODULESTAT, CLIENT STORE
      WHERE ((STKOUT.NUM = STKOUTDTL.NUM)
       and  (STKOUT.CLS = STKOUTDTL.CLS)
       and  (STKOUT.NUM = STKOUTLOG.NUM)
       and  (STKOUT.CLS = STKOUTLOG.CLS)
       and  (STKOUT.STAT = MODULESTAT.NO)
       and  (STKOUTDTL.GDGID = GOODSH.GID)
       and  ((STKOUT.CLS = VCLS AND VCLS IS NOT NULL) OR (STKOUT.CLS ='批发退' AND VCLS IS NULL))
       and  (STKOUT.BILLTO = STORE.GID)
       --and  (STKOUT.STAT = STKOUTLOG.STAT)
       and  (STKOUTLOG.STAT = 1000 )
       and  (STKOUTLOG.TIME >= VBDATE OR VBDATE IS NULL)
       and  (STKOUTLOG.TIME < VEDATE OR VEDATE IS NULL)
       and  (STKOUT.NUM LIKE VNUM||'%' OR VNUM IS NULL)
       and  (GOODSH.CODE LIKE VGDCODE||'%' OR VGDCODE IS NULL));
  COMMIT;
END;
