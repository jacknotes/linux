DECLARE
  VBDATE  DATE;
  VEDATE  DATE;
  VNUM    VARCHAR2(2000);
  VGDCODE  VARCHAR2(20);
  VSTATNAME VARCHAR2(200);
  VFILTER1 VARCHAR2(1000);
  VCLS  VARCHAR2(14);
BEGIN

  VBDATE :=TO_DATE('\(1,1)','YYYY.MM.DD');
  VEDATE :=TO_DATE('\(2,1)','YYYY.MM.DD');
  VNUM :=TRIM('\(3,1)');
  --PPS_CONVERSTR(VFILTER1,',',VNUM);
  VGDCODE:=TRIM('\(4,1)');
 -- VSTATNAME :=TRIM('\(5,1)');
  VCLS := TRIM('\(5,1)');

  DELETE FROM H4RTMP_PHMX;
  COMMIT;
    ---插入直配出数据
  INSERT INTO H4RTMP_PHMX(NUM,CLS,GDGID,GDCODE,GDNAME,GDCODE2,STNAME,BCODE,BNAME,QTY,QTYSTR,PRICE,TOTAL,TAX,CAMT,CTAX,RTOTAL,STATNAME,FILDATE,LINE,STCODE,VDRCODE,VDRNAME,VDRBILLTOCODE,VDRBILLTONAME,QPCSTR)
    SELECT STKOUT.NUM , STKOUT.CLS ,GOODSH.GID, GOODSH.CODE , GOODSH.NAME , GOODSH.CODE2 , 
      STORE.NAME ,B.CODE,B.NAME, STKOUTDTL.QTY , STKOUTDTL.QTYSTR , 
      STKOUTDTL.PRICE , STKOUTDTL.TOTAL , STKOUTDTL.TAX , STKOUTDTL.SCAMT , 
      STKOUTDTL.SCTAX , STKOUTDTL.QTY*STKOUTDTL.CRTLPRC , MODULESTAT.STATNAME,STKOUTLOG.TIME,STKOUTDTL.LINE,STORE.CODE,
      VDR.CODE, VDR.NAME, VDRBILLTO.CODE, VDRBILLTO.NAME, STKOUTDTL.QPCSTR
    FROM DIRALC STKOUT, DIRALCLOG STKOUTLOG, DIRALCDTL STKOUTDTL, GOODSH GOODSH, MODULESTAT MODULESTAT, STORE STORE, VENDOR VDR, VENDOR VDRBILLTO,BRAND B
      WHERE ((STKOUT.NUM = STKOUTDTL.NUM)
       and  (STKOUT.CLS = STKOUTDTL.CLS)
       and  (STKOUT.NUM = STKOUTLOG.NUM)
       and  (STKOUT.CLS = STKOUTLOG.CLS)
       and  (STKOUT.STAT = MODULESTAT.NO)
       AND  (GOODSH.BRAND = B.CODE(+))
       and  (STKOUTDTL.GDGID = GOODSH.GID)
       and  ((STKOUT.CLS = VCLS AND VCLS IS NOT NULL) OR (STKOUT.CLS ='直配出' AND VCLS IS NULL))
       and  (STKOUT.RECEIVER = STORE.GID)
       --and  (STKOUT.STAT = STKOUTLOG.STAT)
       and  (STKOUTLOG.STAT in (1000,1020,1040,320,340))
       and  (STKOUTLOG.TIME >= VBDATE OR VBDATE IS NULL)
       and  (STKOUTLOG.TIME < VEDATE OR VEDATE IS NULL)
       and  (STKOUT.NUM LIKE VNUM||'%' OR VNUM IS NULL)
       and  (GOODSH.CODE LIKE VGDCODE||'%' OR VGDCODE IS NULL) )
       AND   STKOUT.VENDOR = VDR.GID
       AND   STKOUT.BILLTO = VDRBILLTO.GID;
  COMMIT;
    ---插入直配出退数据
  INSERT INTO H4RTMP_PHMX(NUM,CLS,GDGID,GDCODE,GDNAME,GDCODE2,STNAME,BCODE,BNAME,QTY,QTYSTR,PRICE,TOTAL,TAX,CAMT,CTAX,RTOTAL,STATNAME,FILDATE,LINE,STCODE,VDRCODE,VDRNAME,VDRBILLTOCODE,VDRBILLTONAME,QPCSTR)
    SELECT STKOUT.NUM , STKOUT.CLS ,GOODSH.GID, GOODSH.CODE , GOODSH.NAME , GOODSH.CODE2 , 
      STORE.NAME , B.CODE,B.NAME,STKOUTDTL.QTY*(-1) , STKOUTDTL.QTYSTR , 
      STKOUTDTL.PRICE , STKOUTDTL.TOTAL*(-1) , STKOUTDTL.TAX*(-1) , STKOUTDTL.SCAMT*(-1) , 
      STKOUTDTL.SCTAX*(-1) , STKOUTDTL.QTY*STKOUTDTL.CRTLPRC*(-1) , MODULESTAT.STATNAME,STKOUTLOG.TIME,STKOUTDTL.LINE,STORE.CODE,
      VDR.CODE, VDR.NAME, VDRBILLTO.CODE, VDRBILLTO.NAME, STKOUTDTL.QPCSTR
    FROM DIRALC STKOUT, DIRALCLOG STKOUTLOG,DIRALCDTL STKOUTDTL, GOODSH GOODSH, MODULESTAT MODULESTAT, STORE STORE, VENDOR VDR, VENDOR VDRBILLTO,BRAND B
      WHERE ((STKOUT.NUM = STKOUTDTL.NUM)
       and  (STKOUT.CLS = STKOUTDTL.CLS)
       and  (STKOUT.NUM = STKOUTLOG.NUM)
       and  (STKOUT.CLS = STKOUTLOG.CLS)
       and  (STKOUT.STAT = MODULESTAT.NO)
       AND  (GOODSH.BRAND = B.CODE(+))
       and  (STKOUTDTL.GDGID = GOODSH.GID)
       and  ((STKOUT.CLS = VCLS AND VCLS IS NOT NULL) OR (STKOUT.CLS ='直配出退' AND VCLS IS NULL))
       and  (STKOUT.RECEIVER = STORE.GID)
       --and  (STKOUT.STAT = STKOUTLOG.STAT)
       and  (STKOUTLOG.STAT in (700,720,740,1020,1040) )
       and  (STKOUTLOG.TIME >= VBDATE OR VBDATE IS NULL)
       and  (STKOUTLOG.TIME < VEDATE OR VEDATE IS NULL)
       and  (STKOUT.NUM LIKE VNUM||'%' OR VNUM IS NULL)
       and  (GOODSH.CODE LIKE VGDCODE||'%' OR VGDCODE IS NULL))
       AND   STKOUT.VENDOR = VDR.GID
       AND   STKOUT.BILLTO = VDRBILLTO.GID;
  COMMIT;
END;
