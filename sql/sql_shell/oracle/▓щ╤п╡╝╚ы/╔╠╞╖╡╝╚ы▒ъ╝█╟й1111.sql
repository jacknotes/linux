DECLARE
       PISTORECODE VARCHAR2(10);
       PIGDCODE    VARCHAR2(20);
       PIFLAG      INT;
       PIBGNDATE   DATE;
       PIENDDATE   DATE;
       PIQTY	   VARCHAR2(2);
       PINUM	    VARCHAR2(20); 
    CURSOR C IS SELECT GDCODE,COUNT(GDCODE) VCOUNT FROM H4RTMP_PRICEADJ GROUP BY GDCODE HAVING COUNT(GDCODE)>1;
      
BEGIN
     DELETE FROM H4RTMP_PRICEADJ;
     COMMIT;
     PISTORECODE:='\(1,1)';
     PIGDCODE:='\(2,1)';
     PIFLAG:=\(3,1);
     PIBGNDATE:='\(4,1)';
     PIENDDATE:='\(5,1)';
     PINUM:='\(6,1)';
     PIQTY:='\(7,1)';

	       BEGIN
     		IF (PIFLAG=1)-----商品来源于商品资料
      		  THEN 
      		  INSERT INTO H4RTMP_PRICEADJ(GDCODE,GDNAME,SCODE,SNAME,FILDATE,FLAG,INVQTY)
      		  SELECT B.CODE ,B.NAME||'['||B.SPEC||']',C.CODE,C.NAME,PIBGNDATE,PIFLAG,PIQTY
      		   from GDSALESCHEMEDTL A, Goods B, Store C
      		   where A.GDGID=B.GID
      	  	   and A.CODE=C.GDSALESCHEME
         	  AND B.CREATEDATE>=NVL(PIBGNDATE,'2000.01.01')
	          AND B.CREATEDATE<PIENDDATE
       		  and C.CODE=PISTORECODE
       		  AND B.CODE LIKE PIGDCODE||'%';
    		ELSIF (PIFLAG=-1) THEN -----商品来源于售价改价单
      		  INSERT INTO H4RTMP_PRICEADJ(GDCODE,GDNAME,SCODE,SNAME,FILDATE,FLAG,INVQTY)
      		  SELECT DISTINCT B.CODE ,B.NAME||'['||B.SPEC||']',C.CODE,C.NAME,F.FILDATE,PIFLAG,PIQTY
        	  FROM GDSALESCHEMEDTL A, Goods B, Store C,RPrcAdjDtl D,RPrcAdjLac E,RPrcAdj F
        	   WHERE A.GDGID=B.GID 
        	   and A.CODE=C.GDSALESCHEME
        	   AND D.NUM=E.NUM
       		   AND D.NUM=F.NUM
        	   AND E.STOREGID=C.GID
        	   AND D.GDGID=A.GDGID
        	   AND F.FILDATE>=NVL(PIBGNDATE,'2000.01.01')
	           AND F.FILDATE<PIENDDATE
	           AND F.STAT=800
         	   AND C.CODE=PISTORECODE
         	   --AND B.CREATEDATE>=NVL(PIBGNDATE,'2000.01.01')
	 	  --AND B.CREATEDATE<PIENDDATE
	 	  AND B.CODE LIKE PIGDCODE||'%';

      		  INSERT INTO H4RTMP_PRICEADJ(GDCODE,GDNAME,SCODE, SNAME,FILDATE,FLAG,INVQTY)
     		  select DISTINCT  G.Code , trim(G.Name)  ||  '['  ||  trim(G.Spec)  ||  ']' Name, 
    		    S.CODE,S.name,Q.FILDATE,PIFLAG,PIQTY
   		   from RPrcAdjGdDtl P   , Goods G   , Store S,RPrcAdjGd Q
    		   where P.num=Q.num
    		   and P.grpcode=S.grpcode
    		   AND Q.GDGID=G.GID
   		   and Q.stat=100
    		   AND Q.FILDATE>=NVL(PIBGNDATE,'2000.01.01')
    		   AND Q.FILDATE<PIENDDATE
    		   AND S.CODE=PISTORECODE
    		   AND G.CODE LIKE PIGDCODE||'%';
		
		 ELSIF (PIFLAG=2) THEN -------商品来源于配货出货单
		   INSERT INTO H4RTMP_PRICEADJ(NUM,CLS,GDCODE,GDNAME,SCODE, SNAME,FILDATE,FLAG,INVQTY)
     		     select DISTINCT P.NUM,'统配出',G.Code , trim(G.Name)  ||  '['  ||  trim(G.Spec)  ||  ']' Name, 
    		    S.CODE,S.name,P.FILDATE,PIFLAG,PIQTY
   		   from STKOUT P   , Goods G   , Store S,STKOUTDTL Q
    		   where P.num=Q.num
	           and P.NUM LIKE PINUM||'%'
		   AND P.CLS='统配出'
		   and P.CLS=Q.CLS
    		   AND Q.GDGID=G.GID
		   and P.BILLTO=S.GID
		   and P.stat<>0
   		   and MOD(P.stat,100)=0
    		   AND P.FILDATE<PIENDDATE
		   AND P.FILDATE>=PIBGNDATE
    		   AND S.CODE=PISTORECODE
    		   AND G.CODE LIKE PIGDCODE||'%';

		 ELSIF (PIFLAG=3) THEN -------商品来源于采购员分货单
		   INSERT INTO H4RTMP_PRICEADJ(NUM,CLS,GDCODE,GDNAME,SCODE, SNAME,FILDATE,FLAG,INVQTY)
     		     select DISTINCT P.NUM,'分货单',G.Code , trim(G.Name)  ||  '['  ||  trim(G.Spec)  ||  ']' Name, 
    		    S.CODE,S.name,P.FILDATE,PIFLAG,PIQTY
   		   from ALCBYGOODS P   , Goods G   , Store S,alcbygoodsdtl Q --按商品分货
    		   where P.num=Q.num
	           and P.NUM LIKE PINUM||'%'
    		   AND P.GDGID=G.GID
		   and Q.STOREGID=S.GID
   		   and P.stat=300
    		   AND P.FILDATE<PIENDDATE
		   AND P.FILDATE>=PIBGNDATE
    		   AND S.CODE=PISTORECODE
    		   AND G.CODE LIKE PIGDCODE||'%';

		   INSERT INTO H4RTMP_PRICEADJ(NUM,CLS,GDCODE,GDNAME,SCODE, SNAME,FILDATE,FLAG,INVQTY)
     		     select DISTINCT P.NUM,'分货单',G.Code , trim(G.Name)  ||  '['  ||  trim(G.Spec)  ||  ']' Name, 
    		    S.CODE,S.name,P.FILDATE,PIFLAG,PIQTY
   		   from ALCBYSTORE P   , Goods G   , Store S,ALCBYSTOREDTL Q ,ALCBYSTOREDTL2 DTL2 --  按门店分货
    		   where P.num=Q.num
	           and P.NUM LIKE PINUM||'%'
		   AND P.NUM=DTL2.NUM
    		   AND Q.GDGID=G.GID
		   and DTL2.STOREGID=S.GID
   		   and P.stat=300
    		   AND P.FILDATE<PIENDDATE
		   AND P.FILDATE>=PIBGNDATE
    		   AND S.CODE=PISTORECODE
    		   AND G.CODE LIKE PIGDCODE||'%';

		 END IF;
		END;

      
   commit;
   
   IF PIQTY=1 THEN 
      DELETE FROM H4RTMP_PRICEADJ WHERE NOT EXISTS (SELECT 1 FROM BUSINVS A,STORE B ,GOODS C
 	 WHERE C.CODE=H4RTMP_PRICEADJ.GDCODE AND A.QTY>0 AND C.GID=A.GDGID AND A.STORE=B.GID AND B.CODE=PISTORECODE);
   END IF;

   COMMIT;
    
 
   FOR R IN C LOOP
     DELETE FROM  H4RTMP_PRICEADJ WHERE GDCODE=R.GDCODE AND ROWNUM>1;
            --AND FILDATE<>(SELECT MAX(FILDATE) FROM H4RTMP_PRICEADJ WHERE GDCODE=R.GDCODE );
   END LOOP;	
   COMMIT;
END;
