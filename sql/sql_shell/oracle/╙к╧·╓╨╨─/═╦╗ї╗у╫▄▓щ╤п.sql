  Bfiledate date;
  Efiledate date;
begin

  Bfiledate := '\(1,1)';
  Efiledate := '\(2,1)';

  /*Bfiledate:='2016-09-01';
  Efiledate:='2016-11-01';*/

  delete from tmp_test2;
  commit;

  insert into tmp_test2
    (date1, char1, char2, char3, num1, num2, num3)
    select trunc(a.FILDATE, 'MM'),
           b.province,
           b.code,
           b.name,
           sum(a.qty),
           sum(a.AMT + a.tax),
           sum(a.RTOTAL)
      from sdrpt a, store b
     where a.snd = b.gid
       and cls = '零售退'
       and trunc(a.FILDATE, 'MM') >= trunc(Bfiledate, 'MM')
       and trunc(a.FILDATE, 'MM') <  trunc(Efiledate, 'MM')
     group by trunc(a.FILDATE, 'MM'), b.code, b.name, b.province;

  commit;
end;
