declare
  Bfiledate date;
  Efiledate date;
begin

  Bfiledate := '\(1,1)';
  Efiledate := '\(2,1)';

  /*Bfiledate:='2017-01-01';
  Efiledate:='2017-03-01';*/

  delete from tmp_test2;
  commit;

insert into tmp_test2
(date1,
 char1,
 char2,
 char3,
 char4,
 char5,
 num1,
 num2,
 num3,
 num4,
 num5,
 num6
)
select trunc(a.fildate,'MM'),
       b.province,
       b.code,
       b.name,
       case when b.storetype=1 then '合作店'
            when b.storetype=3 then '直营店'
            when b.storetype=4 then '加盟店' 
       else '其他' end,
       b.storemanagertype,
       sum(decode(a.cls,'零售',1,-1)*a.qty) qty,
       sum(decode(a.cls,'零售',1,-1)*(a.AMT+a.TAX)) total,
       sum(decode(a.cls,'零售',1,-1)*a.AMT) amt,
       sum(decode(a.cls,'零售',1,-1)*a.qty*c.invprc) kc_total,
       d.salecounts ,
       sum(decode(a.cls,'零售',1,-1)*(a.AMT+a.TAX))/d.salecounts  kdj
  from sdrpt a
       left join  store b   on (a.snd = b.gid)
       left join  actinvs c on (a.GDGID=c.gdgid and a.snd=c.store)
       left join (select trunc(a.adate,'MM') adate,b.storegid orgkey , sum(a.dn1) salecounts from cshdrpts a , workstation b
        where a.posno = b.no 
        and trunc(a.adate,'MM')>=trunc(Bfiledate,'MM')
        and trunc(a.adate,'MM')<trunc(Efiledate,'MM')
        group by b.storegid,trunc(a.adate,'MM')) d on (a.snd=d.orgkey and trunc(a.fildate,'MM')=d.adate)
 where  cls in ('零售', '零售退')
   and trunc(a.fildate,'MM')>=trunc(Bfiledate,'MM')
   and trunc(a.fildate,'MM')<trunc(Efiledate,'MM')
   group by trunc(a.fildate,'MM'),
       b.province,
       b.code,
       b.name,
       b.storetype,
       b.storemanagertype,d.salecounts;
        commit;
end;
