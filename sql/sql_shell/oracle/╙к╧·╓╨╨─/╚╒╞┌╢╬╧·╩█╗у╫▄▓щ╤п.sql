declare 
  Bfiledate date;
  Efiledate date;
  
begin
   Bfiledate := to_date('\(1,1)');
   Efiledate := to_date('\(2,1)');
 
delete from tmp_test2;
  commit;  

insert into tmp_test2
(
      int1        --店铺gid                               
     ,char1       --店号
     ,char2       --店名
     ,char3       --店铺所属者
     ,char4       --店铺性质
     ,char5       --店铺管理权限
     ,date1       --开业日期                                                            
     ,num1        --销量
     ,num2        --销售总额
     ,int2        --单据数
     ,date2       --查询开始日期
     ,date3       --查询结束日期
     ,date4       --记账日期
     ,int3        --销售天数 
     ,char6       --区域
     ,char7       --省份
)
select  AA.gid                          --店铺gid
       ,AA.code                         --店号              
       ,AA.name                         --店名
       ,AA.storeowner                   --店铺所属者
       ,AA.storetype                    --店铺性质
       ,AA.storemanagertype             --店铺管理权限
       ,AA.busdate                      --开业日期
       ,sum(AA.qty)                     --销量
       ,sum(AA.amt)                     --销售总额 
       ,sum(BB.salecounts)              --客流量
       ,Bfiledate                       --查询开始日期
       ,Efiledate                       --查询结束日期
       ,AA.fildate                      --记账日期
       ,case when to_date(AA.busdate) > to_date(Bfiledate) then (to_date(Efiledate)-to_date(AA.busdate)+1)
             when to_date(AA.busdate) <=to_date(Bfiledate) then (to_date(Efiledate)-to_date(Bfiledate)+1)
        end days1                     --销售天数   
       ,AA.aname                      --区域  
       ,AA.PROVINCE                   --省份
from       

(select store.gid gid
       ,store.code code
       ,store.name name
       ,trim(store.storeowner) storeowner
       ,case when store.storetype=1 then '合作店'
             when store.storetype=2 then '分公司'
             when store.storetype=3 then '直营店'  
             when store.storetype=4 then '加盟店'
        end as storetype
       ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*sdrpts.qty) qty     
       ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax)) amt 
       ,store.storemanagertype
       ,store.busdate
       ,sdrpts.fildate
       ,area.name aname
       ,store.PROVINCE
        
from store ,sdrpts ,goodsh ,sort ,area
where store.gid = sdrpts.snd
  and sdrpts.gdgid = goodsh.gid
  and substr(goodsh.sort,0,2) = sort.code
  and sdrpts.cls in ( '零售','零售退','批发','批发退','成本差异','成本调整')
  and sdrpts.fildate >= Bfiledate  and sdrpts.fildate<= Efiledate 
  and store.area=area.code
group by store.gid
        ,store.code
        ,store.name
        ,trim(store.storeowner) 
        ,storetype
        ,store.storemanagertype
        ,store.busdate
        ,sdrpts.fildate
        ,area.name
        ,store.PROVINCE
)AA left join
(
select b.storegid gid
      ,s.code
      ,s.name 
      ,sum(a.dn1) salecounts 
      ,a.adate    
 from cshdrpts a ,workstation b ,store s
where a.posno = b.no
  and b.storegid=s.gid
  AND a.adate between Bfiledate and Efiledate
group by b.storegid
        ,s.code
        ,s.name
        ,a.adate
order by s.code
)BB
on AA.gid=BB.gid and AA.fildate=BB.adate
group by AA.gid
        ,AA.code   
        ,AA.name   
        ,AA.storetype  
        ,AA.storeowner
        ,AA.storemanagertype
        ,AA.busdate
        ,AA.fildate
        ,case when to_date(AA.busdate) > to_date(Bfiledate) then (to_date(Efiledate)-to_date(AA.busdate)+1)
             when to_date(AA.busdate) <=to_date(Bfiledate) then (to_date(Efiledate)-to_date(Bfiledate)+1)
         end
        ,AA.aname 
        ,AA.PROVINCE  
        ;
        
commit;
end;
