declare
  Bfiledate  date;
  Efiledate  date;
  days       int;

begin 
  Efiledate:='\(1,1)';
  days :='\(2,1)'; 
  
select (Efiledate - days + 1) into Bfiledate  from dual;
         
delete  from tmp_test2;
commit;  

insert into tmp_test2
(
       char1 --店铺gid
      ,char2 --店号
      ,char3 --店名
      ,char4 --省份
      ,char5 --督导
      ,char6 --品类
      ,char7 --大类
      ,char8 --条形码
      ,char9 --商品名称
      ,int1  --销售数量
      ,int2  --库存数量
      ,date1 --首次发货日期
      ,date2 --查询结束日期
      ,date3 --查询开始日期
      ,int3  --销售天数
      
)
select AA.gid
      ,AA.code
      ,AA.name
      ,AA.province
      ,AA.supervise
      ,AA.sortname1
      ,AA.sortname2
      ,AA.code2
      ,AA.goodsname
      ,AA.qty
      ,BB.qty
      ,AA.outdate
      ,Efiledate
      ,Bfiledate
      ,days
      
from
(
select store.gid gid                --店铺gid
      ,store.code code              --店号
      ,store.name name              --店名
      ,store.province province      --省份
      ,store.supervise supervise    --督导
      ,sort1.name sortname1         --品类
      ,sort2.name sortname2         --大类
      ,goods.code2 code2            --条形码
      ,goods.name  goodsname        --商品名称
      ,goods.gid   goodsgid         --商品gid
      ,sum(sdrpts.qty) qty          --销售数量
     -- ,sum(businvs.qty) qty1        --库存数量
      ,goodsoutsaledatestore.outdate outdate  --首次发货日期
     
      
     
from store , sdrpts ,goods ,sort sort1 ,sort sort2 ,goodsoutsaledatestore  

where store.gid = sdrpts.snd

and sdrpts.gdgid = goods.gid

and substr(goods.sort ,0,2) = sort1.code

and substr(goods.sort ,0,4) = sort2.code

and goods.gid = goodsoutsaledatestore.gid

and store.gid = goodsoutsaledatestore.store

and sdrpts.fildate>= Bfiledate and sdrpts.fildate <= Efiledate

and goodsoutsaledatestore.outdate + 20 >= Efiledate 

group by store.gid
        ,store.code
        ,store.name
        ,store.province
        ,store.supervise
        ,sort1.name
        ,sort2.name
        ,goods.code2
        ,goods.name
        ,goods.gid
        ,goodsoutsaledatestore.outdate
        
order by store.code , sort1.name 
)AA
left join (select   store.gid gid,
                    store.code code,
                    store.name name,
                    goods.gid goodsgid,
                    sum(businvs.qty) qty, --门店库存数量
                    sum(businvs.qty*RPGGD.Rtlprc) totalamt --门店库存金额=库存数量*零售价
                        
               from store, businvs ,RPGGD ,goods
              where store.gid = businvs.store
                and businvs.gdgid=goods.gid
                and businvs.gdgid=RPGGD.Gdgid and goods.code=RPGGD.Inputcode   --取零售价
                and businvs.store <> '1000000' --排除总仓的库存，取店铺对应库存
                               
              group by store.gid, store.code, store.name ,goods.gid
              ) BB
on AA.gid = BB.gid and AA.goodsgid = BB.goodsgid
order by AA.code , AA.sortname1
;

commit;
end;     

