declare
  Efiledate date;
  Bfiledate date;
  days int;
  
begin 
  Bfiledate:='\(1,1)';
  Efiledate:='\(2,1)';
  
select  (Efiledate -Bfiledate + 1) into days from dual;    


delete from tmp_test2;
  commit;

insert into tmp_test2
(
       char1       --店铺gid
      ,char2       --店号
      ,char3       --店名
      ,char4       --省份
      ,char5       --督导
      ,char6       --品类
      ,char7       --大类
      ,char8       --小类
      ,num1        --销售数量
      ,num2        --销售金额
      ,num3        --成本金额
      ,num4        --店铺库存
      ,num5        --总仓库存
      ,num6        --总仓在单量
      ,num7        --按门店小类销售额汇总
      ,date1       --查询开始日期
      ,date2       --查询结束日期
      ,int1        --查询天数  
)
select store.gid             --店铺gid
      ,store.code            --店号
      ,store.name            --店名
      ,store.province        --省份
      ,store.supervise       --督导
      ,sort1.name            --品类
      ,sort2.name            --大类
      ,sort3.name            --小类
      ,sal.salqty            --销售数量
      ,sal.salamt            --销售金额
      ,sal.iamt              --成本金额
      ,storebusinvs.dpqty    --店铺库存
      ,zqbusinvs.zqqty       --总仓库存
      ,zqbusinvs.zqordqty    --总仓在单量
      ,saltotal.amt1         --按门店小类销售额汇总
      ,Bfiledate             --查询开始日期
      ,Efiledate             --查询结束日期
      ,days                  --查询天数
      
from
( --销售信息临时表
select sdrpts.snd snd
      ,goods.gid  gdgid
      ,substr(goods.sort , 0 , 6) s
      ,sum(decode(sdrpts.cls ,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*sdrpts.qty) salqty
      ,sum(decode(sdrpts.cls ,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax)) salamt
      ,sum(decode(sdrpts.cls ,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.iamt+sdrpts.itax)) iamt
    
         
from  sdrpts ,goods 

where sdrpts.gdgid = goods.gid 

and sdrpts.cls in ( '零售','零售退','批发','批发退','成本差异','成本调整')

and sdrpts.fildate >= Bfiledate and sdrpts.fildate <= Efiledate

group by sdrpts.snd , substr(goods.sort , 0 , 6)  , goods.gid ) sal ,

( --店铺库存
select businvs.store storegid
      ,goods.gid gdgid
      ,substr(goods.sort , 0 , 6) s
      ,sum(businvs.qty) dpqty
      
from businvs ,goods
where businvs.gdgid = goods.gid 
and businvs.store <> 1000000
group by businvs.store , goods.gid ,substr(goods.sort , 0 , 6) ) storebusinvs ,

( --总仓库存及在单量
select goods.gid gdgid
      ,substr(goods.sort , 0 , 6) s
      ,sum(businvs.qty) zqqty
      ,sum(businvs.ordqty) zqordqty
from businvs ,goods
where businvs.gdgid = goods.gid
and businvs.store = 1000000
group by goods.gid ,substr(goods.sort , 0 , 6)) zqbusinvs ,

(--按店铺按小类销售额汇总
 select sdrpts.snd snd
       ,substr(goods.sort,0,6) s
       ,sum(decode(sdrpts.cls ,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(sdrpts.amt+sdrpts.tax)) amt1
 from sdrpts , goods
 where sdrpts.gdgid = goods.gid
   and sdrpts.cls in ( '零售','零售退','批发','批发退','成本差异','成本调整') 
   and sdrpts.fildate >= Bfiledate and sdrpts.fildate <= Efiledate
 group by sdrpts.snd , substr(goods.sort,0,6)  
) saltotal,

 store ,
 sort sort1,
 sort sort2,
 sort sort3,
 goods 
 
 where sal.snd = store.gid
   and sal.gdgid = goods.gid
   and sal.snd = storebusinvs.storegid(+)
   and sal.gdgid = storebusinvs.gdgid(+)
   and sal.s = storebusinvs.s(+)
   and sal.gdgid = zqbusinvs.gdgid(+)
   and sal.s = zqbusinvs.s(+)
   and substr(sal.s , 0 ,2) = sort1.code
   and substr(sal.s , 0 ,4) = sort2.code
   and substr(sal.s , 0 ,6) = sort3.code  
   and sal.snd = saltotal.snd(+)
   and sal.s = saltotal.s(+)
   
   order by store.code , sort3.name ;
   
commit;
end;   
  

   
   
   
