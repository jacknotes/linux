insert into tmp_test2
    (char1,
     char2,
     date1,
     char3,
     num1,
     num2
    )
select  
          STORE.CODE,                      --门店代码
          STORE.NAME,                      --门店名称
          trunc(postime,'MM'),
          store.province,                  --省份
          sum(QUANTITY),                   --删除数量
          sum(AMOUNT)                      --删除金额
from OPERLOG_REDEAL,STORE,WORKSTATION
where OPERLOG_REDEAL.Posno=WORKSTATION.No
      and store.gid=WORKSTATION.Storegid
      and(POSTIME >= Bfiledate  and POSTIME < Efiledate)
      and (PROVINCE=PROVINCE1 or PROVINCE1 is null )
      and (STORE.CODE=StoreCode or StoreCode is null)

group by 
          STORE.CODE,                      
          STORE.NAME,
          store.province,                     
           trunc(postime,'MM')
order by   trunc(postime,'MM');

commit;
end;
