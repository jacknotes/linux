  Bfiledate date;
  Efiledate date;
  StoreCode  varchar2(15);
  PROVINCE1  varchar2(15);

begin
  
  StoreCode := '\(1,1)';
  Bfiledate := to_date('\(2,1)', 'yyyy.mm.dd');
  Efiledate := to_date('\(3,1)', 'yyyy.mm.dd');
  PROVINCE1 :='\(4,1)';
  
