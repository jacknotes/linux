declare
  Bfiledate date;
  Efiledate date;

begin

  Bfiledate := '\(1,1)';
  Efiledate := '\(2,1)';

  /*Bfiledate:='2016-09-01';
  Efiledate:='2016-10-31';*/

  delete from tmp_test2;
  commit;
  insert into tmp_test2
    (char1, --店号
     char2, --店名
     date1, --接收时间
     char3, --pos号
     char4, --商品条码
     char5, --商品代码
     char6, --商品名称
     char7, --品类
     char8, --小类
     num1, --单价
     num2, --数量
     num3, --应收金额
     num4, --实收金额
     char9, --流水号
     char10 --省份
     )
    select b.code,
           b.name,
           a.rcvtime,
           a.posno,
           c.code2,
           c.code,
           c.name,
           d.asname,
           d.dsname,
           c.rtlprc,
           sum(a.qty),
           sum(a.SCRTOTAL),
           sum(a.REALAMT),
           a.flowno,
           b.province
      FROM buy2 a
      left join (select a.code, a.name, a.province, b.no
                   from store a, workstation b
                  where a.gid = b.storegid) b
        on a.posno = b.no
      left join goods c
        on a.GID = c.gid
      left join h4v_goodssort d
        on a.gid = d.gid
     where a.qty < 0
       and a.rcvtime >= Bfiledate
       and a.rcvtime < Efiledate
     group by b.code,
              b.name,
              a.rcvtime,
              a.posno,
              c.code2,
              c.code,
              c.name,
              d.asname,
              d.dsname,
              c.rtlprc,
              a.flowno,
              b.province;
  commit;
end;
