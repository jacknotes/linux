declare
  Bfiledate date;
  Efiledate date;
  cfaseason varchar2(100);

begin
  Bfiledate:='\(1,1)';
  Efiledate:='\(2,1)';
  cfaseason:= trim('\(3,1)');
 
  delete from tmp_test2;
  commit;

insert into tmp_test2
(
       date1    --记账日
      ,int1     --店铺gid
      ,char1    --店号
      ,char2    --店名
      ,char3    --品类代码
      ,char4    --品类
      ,char5    --大类代码
      ,char6    --大类
      ,char7    --季节
      ,num1     --销量
      ,num2     --销额
      ,date2    --开始日期
      ,date3    --结束日期
      
      
)
select AA.fildate
      ,AA.gid
      ,AA.code
      ,AA.name
      ,AA.ascode
      ,AA.asname
      ,AA.bscode
      ,AA.bsname
      ,max(AA.c_faseason)
      ,sum(AA.salqty)
      ,sum(AA.salamt)
      ,Bfiledate           
      ,Efiledate           
from
(select trunc(sdrpts.fildate) fildate
      ,store.gid
      ,store.code
      ,store.name
      ,h4v_goodssort.ascode
      ,h4v_goodssort.asname
      ,h4v_goodssort.bscode
      ,h4v_goodssort.bsname
      ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*sdrpts.qty) salqty
      ,sum(decode(sdrpts.cls,'零售',1,'批发',1,'成本差异',0,'成本调整',0,-1)*(nvl(sdrpts.amt,0)+nvl(sdrpts.tax,0))) salamt
      ,goods.c_faseason
             

 from sdrpts ,h4v_goodssort,store ,goods
where sdrpts.fildate between Bfiledate and Efiledate
  and sdrpts.cls in ( '零售','零售退','批发','批发退','成本差异','成本调整')
  and sdrpts.gdgid=h4v_goodssort.gid
  and sdrpts.snd=store.gid
  and sdrpts.gdgid=goods.gid
  and (h4v_goodssort.ascode like '1%'
       or h4v_goodssort.ascode like '26%')
  and cfaseason like '%' || goods.c_faseason || '%' 

group by store.gid
        ,store.code
        ,store.name
        ,h4v_goodssort.ascode
        ,h4v_goodssort.asname
        ,h4v_goodssort.bscode
        ,h4v_goodssort.bsname
        ,trunc(sdrpts.fildate)
        ,goods.c_faseason       
order by store.gid ,trunc(sdrpts.fildate) ,h4v_goodssort.ascode  ,h4v_goodssort.bscode
)AA
group by AA.fildate
        ,AA.gid
        ,AA.code
        ,AA.name
        ,AA.ascode
        ,AA.asname
        ,AA.bscode
        ,AA.bsname

;  
commit;
end;
