[标题]
门店单品流水帐
[应用背景]

[结果列描述]
数量（+）：增加库存数
数量（-）：减少库存数
数量平衡：对应业务发生后库存数量
[必要的查询条件]
期号：必选
商品代码：必选
门店代码：必选

[实现方法]
1.期初的数量：输入期号的上一期数量。果期号为当前期，期初数量为上一期数量，期末可能不
是当前库
存。
2.销售数量：取值日报表，数量不精确到时分秒。不能完全作为快照数量的对比依据。
