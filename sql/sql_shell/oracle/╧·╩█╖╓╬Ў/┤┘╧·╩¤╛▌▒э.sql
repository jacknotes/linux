declare
  Bfiledate date; 
  Efiledate date;
  
begin
  
  Bfiledate := to_date('\(1,1)', 'yyyy.mm.dd');
  Efiledate := to_date('\(2,1)', 'yyyy.mm.dd');

 /* Bfiledate := to_date('2017-9-22', 'yyyy.mm.dd');
