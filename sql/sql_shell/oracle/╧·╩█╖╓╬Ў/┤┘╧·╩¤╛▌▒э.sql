select d.province province,          --省份
       d.code code,                  --店号
       d.name name,                  --店名
       d.gid gid,                   --门店gid
       c.asname asname,                --品类
       c.bsname bsname,                --大类
       b.code2 code2,                 --商品条码
       a.gdgid gdgid,                 --商品gid
       b.name gname,                  --商品名称
       b.RTLPRC RTLPRC,                --商品价格
       a.promcls promcls,               --促销类型
       a.promnum promnum,               --促销单号
       E.name Ename,                         --活动名称
       sum(a.promqty) promqty,          --参与促销数量 
       sum(a.FAVAMOUNT) FAVAMOUNT,        --优惠金额 
       sum(a.promamt)promamt,            --参与促销金额 
       Bfiledate,
       Efiledate

from PROMDRPTS a left join  goods b on a.gdgid=b.gid
                 left join  h4v_goodssort c on a.gdgid=c.gid
                 left join  store d on a.storegid=d.gid  
                 left join (select a.num,a.cls,b.name from prom  a left join PrmTopic b on  a.topic=b.code
union all
select a.num,a.cls,b.name from promA  a left join PrmTopic b on  a.topic=b.code
union all
select a.num,'组合',b.name from promB  a left join PrmTopic b on  a.topic=b.code
union all
select a.num,'组合',b.name from promC  a left join PrmTopic b on  a.topic=b.code)E on (E.num=a.promnum and e.cls=a.promcls)
                         
where  a.adate >=Bfiledate
       and a.adate <=Efiledate 
       and a.promqty != 0 
group by d.province,              --省份
       d.code,                  --店号
       d.name,                  --店名
       d.gid,                   --门店gid
       c.asname,                --品类
       c.bsname,                --大类
       b.code2,                 --商品条码
       a.gdgid,                 --商品gid
       b.name,                  --商品名称
       b.RTLPRC,                --商品价格
       a.promcls,               --促销类型
       a.promnum,               --促销单号
       E.name
        ;
commit;
end;
