DECLARE
vbegdate date;
venddate date;
BEGIN
  vbegdate := to_date('\(1,1)','yyyy.mm.dd');
  venddate := to_date('\(2,1)','yyyy.mm.dd');
DELETE FROM HDTMP_BUYBRAND;
COMMIT;
INSERT INTO HDTMP_BUYBRAND(POSNO,FLOWNO,ITEMNO,GDNAME,GDCODE,QTY,STDTOTAL,REALAMT,STCODE,STNAME,BUSDATE,EMPCODE,EMPNAME,
SORT,DSCODE,DSNAME,FAVAMT1,FAVAMT2,FAVAMT3)
SELECT BUY1S.POSNO POSNO, BUY1S.FLOWNO FLOWNO, BUY2S.ITEMNO ITEMNO, GOODS.NAME GDNAME, GOODS.CODE GDCODE, 
BUY2S.QTY QTY, BUY2S.STDTOTAL STDTOTAL,BUY2S.REALAMT REALAMT, STORE.CODE STCODE, STORE.NAME STNAME, 
BUY1S.BUSDATE BUSDATE, EMPLOYEEH.CODE EMPCODE, EMPLOYEEH.NAME EMPNAME, SORT.NAME||SORT.CODE  SORT, 
h4v_goodsbrand.dscode dscode, h4v_goodsbrand.dsname dsname,0,0,0
FROM BUY1S BUY1S, BUY2S BUY2S, GOODS GOODS, WORKSTATION WORKSTATION, STORE STORE, EMPLOYEEH EMPLOYEEH, SORT SORT, h4v_goodsbrand h4v_goodsbrand
WHERE (BUY2S.GID = GOODS.GID)
 and  (BUY1S.POSNO = WORKSTATION.NO)
 and  (WORKSTATION.STOREGID = STORE.GID)
 and  (BUY2S.POSNO = BUY1S.POSNO)
 and  (BUY2S.FLOWNO = BUY1S.FLOWNO)
 and  (BUY1S.CASHIER = EMPLOYEEH.GID)
 and  (GOODS.SORT = SORT.CODE)
 and  (GOODS.GID = h4v_goodsbrand.gid(+))
 and (BUY1S.BUSDATE >= vbegdate or vbegdate is null) 
 and (BUY1S.BUSDATE < venddate OR venddate IS NULL);
---自定义关心的促销类型,预留3个类型
UPDATE HDTMP_BUYBRAND A SET FAVAMT1 = (SELECT NVL(SUM(FAVAMT),0) FROM BUY21S WHERE POSNO = A.POSNO 
AND FLOWNO = A.FLOWNO AND ITEMNO = A.ITEMNO AND FAVTYPE = '12');
UPDATE HDTMP_BUYBRAND A SET FAVAMT2 = (SELECT NVL(SUM(FAVAMT),0) FROM BUY21S WHERE POSNO = A.POSNO 
AND FLOWNO = A.FLOWNO AND ITEMNO = A.ITEMNO AND FAVTYPE = '08');
COMMIT;

END;

