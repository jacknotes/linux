declare

vbegdate date;
venddate date;
begin
  vbegdate := to_date('\(1,1)','yyyy.mm.dd');
  venddate := to_date('\(2,1)','yyyy.mm.dd');

delete from H4TMP_BUYCURRENCY;
commit;

insert into H4TMP_BUYCURRENCY(FLOWNO,EMPCODE,EMPNAME,POSNO,STCODE,STNAME,REALAMT,FAVAMOUNT,CURNAME1,AMOUNT1,
CURNAME2,AMOUNT2,CURNAME3,AMOUNT3,CURNAME4,AMOUNT4,CURNAME5,AMOUNT5,CURNAME6,AMOUNT6,
ELSENAME,ELSEAMOUNT,FILDATE,SALECLS,MEMO,BUSDATE)
SELECT BUY1S.FLOWNO FLOWNO, EMPLOYEEH.CODE EMPCODE, EMPLOYEEH.NAME EMPNAME, BUY1S.POSNO POSNO, 
STORE.CODE STCODE, STORE.NAME STNAME, BUY1S.REALAMT REALAMT, SUM(BUY21S.FAVAMT) FAVAMOUNT, 
NULL CURNAME1 ,SUM(decode(BUY21S.FAVTYPE,'01',1,0) * BUY21S.FAVAMT) AMOUNT1, 
NULL CURNAME2 ,SUM(decode(BUY21S.FAVTYPE,'02',1,0) * BUY21S.FAVAMT) AMOUNT2, 
NULL CURNAME3 ,SUM(decode(BUY21S.FAVTYPE,'03',1,0) * BUY21S.FAVAMT) AMOUNT3, 
NULL CURNAME4 ,SUM(decode(BUY21S.FAVTYPE,'04',1,0) * BUY21S.FAVAMT) AMOUNT4,
NULL CURNAME5 ,SUM(decode(BUY21S.FAVTYPE,'05',1,0) * BUY21S.FAVAMT) AMOUNT5, 
NULL CURNAME6 ,SUM(decode(BUY21S.FAVTYPE,'06',1,0) * BUY21S.FAVAMT) AMOUNT6,
'其他' ELSENAME ,SUM((CASE WHEN BUY21S.FAVTYPE>'06' THEN 1 ELSE 0 END) * BUY21S.FAVAMT) ELSEAMOUNT, 
BUY1S.FILDATE FILDATE, (case when  BUY1S.REALAMT > 0 then '销售' else '销售退货' end) salecls, 
BUY1S.MEMO MEMO, BUY1S.BUSDATE BUSDATE
FROM BUY21S BUY21S, STORE STORE, WORKSTATION WORKSTATION, BUY1S BUY1S, EMPLOYEEH EMPLOYEEH
WHERE (BUY21S.POSNO(+) = BUY1S.POSNO)
 and  (BUY21S.FLOWNO(+) = BUY1S.FLOWNO)
 and  (BUY1S.POSNO = WORKSTATION.NO)
 and  (BUY1S.CASHIER = EMPLOYEEH.GID)
 and  (WORKSTATION.STOREGID = STORE.GID) 
 and (BUY1S.BUSDATE >= vbegdate or vbegdate is null) 
 and (BUY1S.BUSDATE < venddate OR venddate IS NULL)
GROUP BY BUY1S.FLOWNO, EMPLOYEEH.CODE, EMPLOYEEH.NAME, BUY1S.POSNO, STORE.CODE, STORE.NAME,
 BUY1S.REALAMT, BUY1S.FILDATE, (case when  BUY1S.REALAMT > 0 then '销售' else '销售退货' end), BUY1S.MEMO, BUY1S.BUSDATE
ORDER BY POSNO ASC, FLOWNO ASC;
